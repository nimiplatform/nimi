#!/usr/bin/env node

import { promises as fs } from 'node:fs';
import path from 'node:path';
import { fileURLToPath } from 'node:url';
import YAML from 'yaml';

const scriptDir = path.dirname(fileURLToPath(import.meta.url));
const repoRoot = path.resolve(scriptDir, '..');

const tablesDir = path.join(repoRoot, 'spec', 'future', 'kernel', 'tables');
const outDir = path.join(repoRoot, 'spec', 'future', 'kernel', 'generated');

const specs = [
  { input: 'backlog-items.yaml', output: 'backlog-items.md', render: renderBacklogItems },
  { input: 'research-sources.yaml', output: 'research-sources.md', render: renderResearchSources },
  { input: 'graduation-log.yaml', output: 'graduation-log.md', render: renderGraduationLog },
];

function normalizeMarkdown(markdown) {
  return `${markdown.replace(/\n{3,}/gu, '\n\n').replace(/\n+$/u, '\n')}`;
}

function header(title, sourceName) {
  return normalizeMarkdown([
    `# ${title}`,
    '',
    '> AUTO-GENERATED by `scripts/generate-future-spec-kernel-docs.mjs`. DO NOT EDIT.',
    `> Source: \`spec/future/kernel/tables/${sourceName}\``,
    '',
  ].join('\n'));
}

function renderBacklogItems(doc, sourceName) {
  const items = Array.isArray(doc?.items) ? doc.items : [];
  let out = header('Generated Backlog Items', sourceName);

  const groups = { high: [], medium: [], low: [] };
  for (const item of items) {
    const priority = String(item?.priority || '').trim();
    if (groups[priority]) {
      groups[priority].push(item);
    }
  }

  for (const [priority, groupItems] of Object.entries(groups)) {
    out += `## ${priority.charAt(0).toUpperCase() + priority.slice(1)} Priority\n\n`;
    if (groupItems.length === 0) {
      out += '_No items._\n\n';
      continue;
    }
    out += '| Item ID | Title | Category | Target Layers | Status | Complexity | Depends On | Sources |\n';
    out += '|---|---|---|---|---|---|---|---|\n';
    for (const item of groupItems) {
      const itemId = String(item?.item_id || '').trim();
      const title = String(item?.title || '').trim();
      const category = String(item?.category || '').trim();
      const targetLayers = Array.isArray(item?.target_layers)
        ? item.target_layers.map((l) => String(l)).join(', ')
        : '';
      const status = String(item?.status || '').trim();
      const complexity = String(item?.complexity || '').trim();
      const dependsOn = Array.isArray(item?.depends_on)
        ? item.depends_on.map((d) => `\`${String(d)}\``).join(', ')
        : '—';
      const sourceIds = Array.isArray(item?.source_ids)
        ? item.source_ids.map((s) => `\`${String(s)}\``).join(', ')
        : '';
      out += `| \`${itemId}\` | ${title} | \`${category}\` | ${targetLayers} | \`${status}\` | \`${complexity}\` | ${dependsOn} | ${sourceIds} |\n`;
    }
    out += '\n';
  }

  out += '## Architecture Notes\n\n';
  for (const item of items) {
    const itemId = String(item?.item_id || '').trim();
    const title = String(item?.title || '').trim();
    const notes = String(item?.architecture_notes || '').trim();
    if (!itemId || !notes) continue;
    out += `### ${itemId}: ${title}\n\n${notes}\n\n`;
  }

  return normalizeMarkdown(out);
}

function renderResearchSources(doc, sourceName) {
  const sources = Array.isArray(doc?.sources) ? doc.sources : [];
  let out = header('Generated Research Sources', sourceName);
  out += '| Source ID | Title | Date | Path | Scope |\n';
  out += '|---|---|---|---|---|\n';
  for (const source of sources) {
    const sourceId = String(source?.source_id || '').trim();
    const title = String(source?.title || '').trim();
    const date = String(source?.date || '').trim();
    const filePath = String(source?.path || '').trim();
    const scope = String(source?.scope || '').trim();
    if (!sourceId) continue;
    out += `| \`${sourceId}\` | ${title} | ${date} | \`${filePath}\` | ${scope} |\n`;
  }
  out += '\n';
  return normalizeMarkdown(out);
}

function renderGraduationLog(doc, sourceName) {
  const entries = Array.isArray(doc?.entries) ? doc.entries : [];
  let out = header('Generated Graduation Log', sourceName);
  if (entries.length === 0) {
    out += '_No graduated items yet._\n';
    return normalizeMarkdown(out);
  }
  out += '| Item ID | Graduated Date | Target Spec Path | Target Rule IDs | Notes |\n';
  out += '|---|---|---|---|---|\n';
  for (const entry of entries) {
    const itemId = String(entry?.item_id || '').trim();
    const graduatedDate = String(entry?.graduated_date || '').trim();
    const targetSpecPath = String(entry?.target_spec_path || '').trim();
    const targetRuleIds = Array.isArray(entry?.target_rule_ids)
      ? entry.target_rule_ids.map((r) => `\`${String(r)}\``).join(', ')
      : '';
    const notes = String(entry?.notes || '').trim();
    if (!itemId) continue;
    out += `| \`${itemId}\` | ${graduatedDate} | \`${targetSpecPath}\` | ${targetRuleIds} | ${notes || '—'} |\n`;
  }
  out += '\n';
  return normalizeMarkdown(out);
}

function renderGeneratedIndex(entries) {
  let out = [
    '# Generated Future Kernel Views',
    '',
    '> AUTO-GENERATED by `scripts/generate-future-spec-kernel-docs.mjs`. DO NOT EDIT.',
    '',
    '| File | Source |',
    '|---|---|',
  ].join('\n');

  for (const entry of entries) {
    out += `\n| \`${entry.output}\` | \`tables/${entry.input}\` |`;
  }
  out += '\n';

  return normalizeMarkdown(out);
}

async function parseYamlFile(filePath) {
  const raw = await fs.readFile(filePath, 'utf8');
  return YAML.parse(raw);
}

async function main() {
  const checkMode = process.argv.includes('--check');

  await fs.mkdir(outDir, { recursive: true });

  const renderedEntries = [];
  for (const spec of specs) {
    const inputPath = path.join(tablesDir, spec.input);
    const outputPath = path.join(outDir, spec.output);
    const parsed = await parseYamlFile(inputPath);
    renderedEntries.push({ ...spec, outputPath, rendered: spec.render(parsed, spec.input) });
  }

  const indexPath = path.join(outDir, 'index.md');
  const indexRendered = renderGeneratedIndex(specs);

  if (checkMode) {
    const drifted = [];
    for (const entry of renderedEntries) {
      let current = '';
      try {
        current = await fs.readFile(entry.outputPath, 'utf8');
      } catch {
        drifted.push(entry.outputPath);
        continue;
      }
      if (current !== entry.rendered) drifted.push(entry.outputPath);
    }

    let currentIndex = '';
    try {
      currentIndex = await fs.readFile(indexPath, 'utf8');
    } catch {
      drifted.push(indexPath);
    }
    if (currentIndex !== indexRendered) drifted.push(indexPath);

    if (drifted.length > 0) {
      process.stderr.write('future kernel generated docs drift detected:\n');
      for (const file of drifted) {
        process.stderr.write(`  - ${path.relative(repoRoot, file)}\n`);
      }
      process.stderr.write('run `pnpm generate:future-spec-kernel-docs` to regenerate.\n');
      process.exitCode = 1;
      return;
    }
    process.stdout.write(`future kernel generated docs are up-to-date (${renderedEntries.length + 1} files)\n`);
    return;
  }

  for (const entry of renderedEntries) {
    await fs.writeFile(entry.outputPath, entry.rendered, 'utf8');
  }
  await fs.writeFile(indexPath, indexRendered, 'utf8');
  process.stdout.write(`generated future kernel docs (${renderedEntries.length + 1} files)\n`);
}

main().catch((error) => {
  process.stderr.write(`generate-future-spec-kernel-docs failed: ${String(error)}\n`);
  process.exitCode = 1;
});
