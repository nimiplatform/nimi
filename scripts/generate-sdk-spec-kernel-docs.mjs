#!/usr/bin/env node

import { promises as fs } from 'node:fs';
import path from 'node:path';
import { fileURLToPath } from 'node:url';
import YAML from 'yaml';

const scriptDir = path.dirname(fileURLToPath(import.meta.url));
const repoRoot = path.resolve(scriptDir, '..');

const tablesDir = path.join(repoRoot, 'spec', 'sdk', 'kernel', 'tables');
const outDir = path.join(repoRoot, 'spec', 'sdk', 'kernel', 'generated');

const specs = [
  { input: 'sdk-surfaces.yaml', output: 'sdk-surfaces.md', render: renderSurfaces },
  { input: 'runtime-method-groups.yaml', output: 'runtime-method-groups.md', render: renderMethodGroups },
  { input: 'import-boundaries.yaml', output: 'import-boundaries.md', render: renderImportBoundaries },
  { input: 'sdk-error-codes.yaml', output: 'sdk-error-codes.md', render: renderSdkErrorCodes },
];

function normalizeMarkdown(markdown) {
  return `${markdown.replace(/\n{3,}/gu, '\n\n').replace(/\n+$/u, '\n')}`;
}

function header(title, sourceName) {
  return normalizeMarkdown([
    `# ${title}`,
    '',
    '> AUTO-GENERATED by `scripts/generate-sdk-spec-kernel-docs.mjs`. DO NOT EDIT.',
    `> Source: \`spec/sdk/kernel/tables/${sourceName}\``,
    '',
  ].join('\n'));
}

function renderSurfaces(doc, sourceName) {
  const items = Array.isArray(doc?.surfaces) ? doc.surfaces : [];
  let out = header('Generated SDK Surfaces', sourceName);
  out += '| Path | Package Subpath | Role |\n';
  out += '|---|---|---|\n';
  for (const item of items) {
    const filePath = String(item?.path || '').trim();
    const subpath = String(item?.package_subpath || '').trim();
    const role = String(item?.role || '').trim();
    if (!filePath || !subpath) continue;
    out += `| \`${filePath}\` | \`${subpath}\` | \`${role || '—'}\` |\n`;
  }
  out += '\n';
  return normalizeMarkdown(out);
}

function renderMethodGroups(doc, sourceName) {
  const groups = Array.isArray(doc?.groups) ? doc.groups : [];
  let out = header('Generated Runtime Method Groups', sourceName);
  for (const group of groups) {
    const name = String(group?.group || '').trim();
    if (!name) continue;
    const methods = Array.isArray(group?.methods) ? group.methods : [];
    out += `## ${name}\n\n`;
    out += `Source: \`${String(group?.source_kernel_table || '').trim() || '—'}\`\n\n`;
    out += '| Method | Source Rule |\n';
    out += '|---|---|\n';
    for (const method of methods) {
      out += `| \`${String(method)}\` | \`${String(group?.source_rule || '').trim() || '—'}\` |\n`;
    }
    out += '\n';
  }
  return normalizeMarkdown(out);
}

function renderImportBoundaries(doc, sourceName) {
  const rules = Array.isArray(doc?.rules) ? doc.rules : [];
  let out = header('Generated Import Boundaries', sourceName);
  for (const rule of rules) {
    const surface = String(rule?.surface || '').trim();
    if (!surface) continue;
    const allowed = Array.isArray(rule?.allowed) ? rule.allowed : [];
    const forbidden = Array.isArray(rule?.forbidden) ? rule.forbidden : [];
    out += `## ${surface}\n\n`;
    out += `Source Rule: \`${String(rule?.source_rule || '').trim() || '—'}\`\n\n`;
    out += `Allowed: ${allowed.length > 0 ? allowed.map((item) => `\`${item}\``).join(', ') : '—'}\n\n`;
    out += `Forbidden: ${forbidden.length > 0 ? forbidden.map((item) => `\`${item}\``).join(', ') : '—'}\n\n`;
  }
  return normalizeMarkdown(out);
}

function renderSdkErrorCodes(doc, sourceName) {
  const codes = Array.isArray(doc?.codes) ? doc.codes : [];
  let out = header('Generated SDK Error Codes', sourceName);
  out += '| Name | Family | Source Rule |\n';
  out += '|---|---|---|\n';
  for (const code of codes) {
    const name = String(code?.name || '').trim();
    const family = String(code?.family || '').trim();
    const sourceRule = String(code?.source_rule || '').trim();
    if (!name) continue;
    out += `| \`${name}\` | \`${family || '—'}\` | \`${sourceRule || '—'}\` |\n`;
  }
  out += '\n';
  return normalizeMarkdown(out);
}

function renderGeneratedIndex(entries) {
  let out = [
    '# Generated SDK Kernel Views',
    '',
    '> AUTO-GENERATED by `scripts/generate-sdk-spec-kernel-docs.mjs`. DO NOT EDIT.',
    '',
    '| File | Source |',
    '|---|---|',
  ].join('\n');

  for (const entry of entries) {
    out += `\n| \`${entry.output}\` | \`tables/${entry.input}\` |`;
  }
  out += '\n';

  return normalizeMarkdown(out);
}

async function parseYamlFile(filePath) {
  const raw = await fs.readFile(filePath, 'utf8');
  return YAML.parse(raw);
}

async function main() {
  const checkMode = process.argv.includes('--check');

  await fs.mkdir(outDir, { recursive: true });

  const renderedEntries = [];
  for (const spec of specs) {
    const inputPath = path.join(tablesDir, spec.input);
    const outputPath = path.join(outDir, spec.output);
    const parsed = await parseYamlFile(inputPath);
    renderedEntries.push({ ...spec, outputPath, rendered: spec.render(parsed, spec.input) });
  }

  const indexPath = path.join(outDir, 'index.md');
  const indexRendered = renderGeneratedIndex(specs);

  if (checkMode) {
    const drifted = [];
    for (const entry of renderedEntries) {
      let current = '';
      try {
        current = await fs.readFile(entry.outputPath, 'utf8');
      } catch {
        drifted.push(entry.outputPath);
        continue;
      }
      if (current !== entry.rendered) drifted.push(entry.outputPath);
    }

    let currentIndex = '';
    try {
      currentIndex = await fs.readFile(indexPath, 'utf8');
    } catch {
      drifted.push(indexPath);
    }
    if (currentIndex !== indexRendered) drifted.push(indexPath);

    if (drifted.length > 0) {
      process.stderr.write('sdk kernel generated docs drift detected:\n');
      for (const file of drifted) {
        process.stderr.write(`  - ${path.relative(repoRoot, file)}\n`);
      }
      process.stderr.write('run `pnpm generate:sdk-spec-kernel-docs` to regenerate.\n');
      process.exitCode = 1;
      return;
    }
    process.stdout.write(`sdk kernel generated docs are up-to-date (${renderedEntries.length + 1} files)\n`);
    return;
  }

  for (const entry of renderedEntries) {
    await fs.writeFile(entry.outputPath, entry.rendered, 'utf8');
  }
  await fs.writeFile(indexPath, indexRendered, 'utf8');
  process.stdout.write(`generated sdk kernel docs (${renderedEntries.length + 1} files)\n`);
}

main().catch((error) => {
  process.stderr.write(`generate-sdk-spec-kernel-docs failed: ${String(error)}\n`);
  process.exitCode = 1;
});
