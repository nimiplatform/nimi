#!/usr/bin/env node

import { promises as fs } from 'node:fs';
import path from 'node:path';
import { fileURLToPath } from 'node:url';
import YAML from 'yaml';

const scriptDir = path.dirname(fileURLToPath(import.meta.url));
const repoRoot = path.resolve(scriptDir, '..');

const tablesDir = path.join(repoRoot, 'spec', 'runtime', 'kernel', 'tables');
const outDir = path.join(repoRoot, 'spec', 'runtime', 'kernel', 'generated');

const specs = [
  {
    input: 'rpc-methods.yaml',
    output: 'rpc-methods.md',
    render: renderRpcMethods,
  },
  {
    input: 'reason-codes.yaml',
    output: 'reason-codes.md',
    render: renderReasonCodes,
  },
  {
    input: 'metadata-keys.yaml',
    output: 'metadata-keys.md',
    render: renderMetadataKeys,
  },
  {
    input: 'provider-catalog.yaml',
    output: 'provider-catalog.md',
    render: renderProviderCatalog,
  },
  {
    input: 'provider-capabilities.yaml',
    output: 'provider-capabilities.md',
    render: renderProviderCapabilities,
  },
  {
    input: 'connector-rpc-field-rules.yaml',
    output: 'connector-rpc-field-rules.md',
    render: renderConnectorRpcFieldRules,
  },
  {
    input: 'job-states.yaml',
    output: 'job-states.md',
    render: renderJobStates,
  },
  {
    input: 'state-transitions.yaml',
    output: 'state-transitions.md',
    render: renderStateTransitions,
  },
];

function normalizeMarkdown(markdown) {
  return `${markdown.replace(/\n{3,}/gu, '\n\n').replace(/\n+$/u, '\n')}`;
}

function header(title, sourceName) {
  return normalizeMarkdown([
    `# ${title}`,
    '',
    '> AUTO-GENERATED by `scripts/generate-runtime-spec-kernel-docs.mjs`. DO NOT EDIT.',
    `> Source: \`spec/runtime/kernel/tables/${sourceName}\``,
    '',
  ].join('\n'));
}

function mdBool(value) {
  return value ? 'true' : 'false';
}

function renderRpcMethods(doc, sourceName) {
  const services = Array.isArray(doc?.services) ? doc.services : [];
  let out = header('Generated RPC Methods', sourceName);

  for (const service of services) {
    const name = String(service?.name || '').trim();
    const methods = Array.isArray(service?.methods) ? service.methods : [];
    if (!name) continue;

    out += `## ${name}\n\n`;
    out += '| Method | Type |\n';
    out += '|---|---|\n';
    for (const method of methods) {
      const methodName = String(method?.name || '').trim();
      const type = String(method?.type || '').trim();
      if (!methodName) continue;
      out += `| \`${methodName}\` | \`${type || 'unknown'}\` |\n`;
    }
    out += '\n';
  }

  return normalizeMarkdown(out);
}

function renderReasonCodes(doc, sourceName) {
  const codes = Array.isArray(doc?.codes) ? doc.codes : [];
  let out = header('Generated ReasonCode Table', sourceName);

  out += '| Name | Value | Family |\n';
  out += '|---|---:|---|\n';
  for (const code of codes) {
    const name = String(code?.name || '').trim();
    const value = Number(code?.value);
    const family = String(code?.family || '').trim();
    if (!name || Number.isNaN(value)) continue;
    out += `| \`${name}\` | ${value} | \`${family || 'UNKNOWN'}\` |\n`;
  }
  out += '\n';

  return normalizeMarkdown(out);
}

function renderMetadataKeys(doc, sourceName) {
  const keys = Array.isArray(doc?.keys) ? doc.keys : [];
  let out = header('Generated Metadata Keys', sourceName);

  out += '| Key | Allowed Values | Required When |\n';
  out += '|---|---|---|\n';
  for (const item of keys) {
    const key = String(item?.key || '').trim();
    const allowed = Array.isArray(item?.allowed_values)
      ? item.allowed_values.map((v) => `\`${String(v)}\``).join(', ')
      : '—';
    const requiredWhen = String(item?.required_when || '').trim() || '—';
    if (!key) continue;
    out += `| \`${key}\` | ${allowed || '—'} | ${requiredWhen} |\n`;
  }
  out += '\n';

  return normalizeMarkdown(out);
}

function renderProviderCatalog(doc, sourceName) {
  const providers = Array.isArray(doc?.providers) ? doc.providers : [];
  let out = header('Generated Provider Catalog', sourceName);

  out += '| Provider | Default Endpoint | Requires Explicit Endpoint |\n';
  out += '|---|---|---|\n';
  for (const item of providers) {
    const provider = String(item?.provider || '').trim();
    const endpoint = item?.default_endpoint == null ? '—' : `\`${String(item.default_endpoint)}\``;
    const requiresExplicit = mdBool(Boolean(item?.requires_explicit_endpoint));
    if (!provider) continue;
    out += `| \`${provider}\` | ${endpoint} | \`${requiresExplicit}\` |\n`;
  }
  out += '\n';

  return normalizeMarkdown(out);
}

function renderProviderCapabilities(doc, sourceName) {
  const providers = Array.isArray(doc?.providers) ? doc.providers : [];
  let out = header('Generated Provider Capabilities', sourceName);

  out += '| Provider | Plane | Execution Module | Managed Connector | Inline | Endpoint Requirement | Source Rules |\n';
  out += '|---|---|---|---|---|---|---|\n';
  for (const item of providers) {
    const provider = String(item?.provider || '').trim();
    if (!provider) continue;

    const plane = String(item?.runtime_plane || '').trim() || 'unknown';
    const moduleName = String(item?.execution_module || '').trim() || 'unknown';
    const managed = mdBool(Boolean(item?.managed_connector_supported));
    const inline = mdBool(Boolean(item?.inline_supported));
    const endpointRequirement = String(item?.endpoint_requirement || '').trim() || 'unknown';
    const sourceRules = Array.isArray(item?.source_rules)
      ? item.source_rules.map((v) => `\`${String(v)}\``).join(', ')
      : '—';

    out += `| \`${provider}\` | \`${plane}\` | \`${moduleName}\` | \`${managed}\` | \`${inline}\` | \`${endpointRequirement}\` | ${sourceRules || '—'} |\n`;
  }
  out += '\n';

  return normalizeMarkdown(out);
}

function renderConnectorRpcFieldRules(doc, sourceName) {
  const rules = Array.isArray(doc?.rules) ? doc.rules : [];
  let out = header('Generated Connector RPC Field Rules', sourceName);

  out += '| RPC | Field | Requirement | Source Rule |\n';
  out += '|---|---|---|---|\n';
  for (const item of rules) {
    const rpc = String(item?.rpc || '').trim();
    const field = String(item?.field || '').trim();
    const requirement = String(item?.requirement || '').trim();
    const sourceRule = String(item?.source_rule || '').trim();
    if (!rpc || !field || !requirement) continue;
    out += `| \`${rpc}\` | \`${field}\` | \`${requirement}\` | \`${sourceRule || '—'}\` |\n`;
  }
  out += '\n';

  return normalizeMarkdown(out);
}

function renderJobStates(doc, sourceName) {
  const states = Array.isArray(doc?.states) ? doc.states : [];
  let out = header('Generated Media Job States', sourceName);

  out += '| State | Terminal |\n';
  out += '|---|---|\n';
  for (const item of states) {
    const state = String(item?.state || '').trim();
    if (!state) continue;
    out += `| \`${state}\` | \`${mdBool(Boolean(item?.terminal))}\` |\n`;
  }
  out += '\n';

  return normalizeMarkdown(out);
}

function renderStateTransitions(doc, sourceName) {
  const machines = Array.isArray(doc?.machines) ? doc.machines : [];
  let out = header('Generated State Transitions', sourceName);

  for (const machine of machines) {
    const machineName = String(machine?.machine || '').trim();
    if (!machineName) continue;

    const states = Array.isArray(machine?.states) ? machine.states : [];
    const transitions = Array.isArray(machine?.transitions) ? machine.transitions : [];

    out += `## ${machineName}\n\n`;
    out += `States: ${states.length > 0 ? states.map((s) => `\`${String(s)}\``).join(', ') : '—'}\n\n`;
    out += '| From | To | Trigger | Source |\n';
    out += '|---|---|---|---|\n';
    for (const edge of transitions) {
      const from = String(edge?.from || '').trim();
      const to = String(edge?.to || '').trim();
      const trigger = String(edge?.trigger || '').trim();
      const source = String(edge?.source || '').trim();
      if (!from || !to || !trigger) continue;
      out += `| \`${from}\` | \`${to}\` | \`${trigger}\` | \`${source || '—'}\` |\n`;
    }
    out += '\n';
  }

  return normalizeMarkdown(out);
}

function renderGeneratedIndex(entries) {
  let out = [
    '# Generated Kernel Views',
    '',
    '> AUTO-GENERATED by `scripts/generate-runtime-spec-kernel-docs.mjs`. DO NOT EDIT.',
    '',
    '| File | Source |',
    '|---|---|',
  ].join('\n');

  for (const entry of entries) {
    out += `\n| \`${entry.output}\` | \`tables/${entry.input}\` |`;
  }
  out += '\n';

  return normalizeMarkdown(out);
}

async function parseYamlFile(filePath) {
  const raw = await fs.readFile(filePath, 'utf8');
  return YAML.parse(raw);
}

async function main() {
  const checkMode = process.argv.includes('--check');

  await fs.mkdir(outDir, { recursive: true });

  const renderedEntries = [];
  for (const spec of specs) {
    const inputPath = path.join(tablesDir, spec.input);
    const outputPath = path.join(outDir, spec.output);

    const parsed = await parseYamlFile(inputPath);
    const rendered = spec.render(parsed, spec.input);
    renderedEntries.push({ ...spec, outputPath, rendered });
  }

  const indexPath = path.join(outDir, 'index.md');
  const indexRendered = renderGeneratedIndex(specs);

  if (checkMode) {
    const drifted = [];

    for (const entry of renderedEntries) {
      let current = '';
      try {
        current = await fs.readFile(entry.outputPath, 'utf8');
      } catch {
        drifted.push(entry.outputPath);
        continue;
      }
      if (current !== entry.rendered) {
        drifted.push(entry.outputPath);
      }
    }

    let currentIndex = '';
    try {
      currentIndex = await fs.readFile(indexPath, 'utf8');
    } catch {
      drifted.push(indexPath);
    }
    if (currentIndex !== indexRendered) {
      drifted.push(indexPath);
    }

    if (drifted.length > 0) {
      process.stderr.write('runtime kernel generated docs drift detected:\n');
      for (const file of drifted) {
        process.stderr.write(`  - ${path.relative(repoRoot, file)}\n`);
      }
      process.stderr.write('run `pnpm generate:runtime-spec-kernel-docs` to regenerate.\n');
      process.exitCode = 1;
      return;
    }

    process.stdout.write(`runtime kernel generated docs are up-to-date (${renderedEntries.length + 1} files)\n`);
    return;
  }

  for (const entry of renderedEntries) {
    await fs.writeFile(entry.outputPath, entry.rendered, 'utf8');
  }
  await fs.writeFile(indexPath, indexRendered, 'utf8');

  process.stdout.write(`generated runtime kernel docs (${renderedEntries.length + 1} files)\n`);
}

main().catch((error) => {
  process.stderr.write(`generate-runtime-spec-kernel-docs failed: ${String(error)}\n`);
  process.exitCode = 1;
});
