import { mkdirSync, rmSync, writeFileSync } from 'node:fs';
import path from 'node:path';
import { REALM_GENERATED_RELATIVE_PATH } from './constants.mjs';
import {
  asRecord,
  escapeSingleQuoteString,
  isValidTypeIdentifier,
  toEnumMemberKey,
  toTypeIdentifier,
  uniqueSymbolName,
} from './model-utils.mjs';

export function writeGeneratedModels(repoRoot, spec) {
  const generatedDir = path.join(repoRoot, REALM_GENERATED_RELATIVE_PATH);
  const modelsDir = path.join(generatedDir, 'models');
  const schemaMap = asRecord(asRecord(asRecord(spec).components).schemas);
  const schemaNames = Object.keys(schemaMap).sort((left, right) => left.localeCompare(right));
  const usedSymbolNames = new Map();

  rmSync(modelsDir, { recursive: true, force: true });
  mkdirSync(modelsDir, { recursive: true });

  for (const schemaName of schemaNames) {
    const schema = asRecord(schemaMap[schemaName]);
    const baseSymbol = isValidTypeIdentifier(schemaName) ? schemaName : toTypeIdentifier(schemaName);
    const symbolName = uniqueSymbolName(baseSymbol, usedSymbolNames);
    const filePath = path.join(modelsDir, `${symbolName}.ts`);
    const enumValues = Array.isArray(schema.enum) ? schema.enum : null;

    const lines = [
      '/* eslint-disable */',
      '// AUTO-GENERATED by scripts/generate-realm-sdk.mjs. DO NOT EDIT.',
      '',
    ];

    if (enumValues && enumValues.length > 0) {
      const usedEnumMemberNames = new Map();
      lines.push(`export const ${symbolName} = {`);
      for (let index = 0; index < enumValues.length; index += 1) {
        const enumValue = enumValues[index];
        const memberName = toEnumMemberKey(enumValue, index, usedEnumMemberNames);
        lines.push(`  ${memberName}: ${JSON.stringify(enumValue)},`);
      }
      lines.push('} as const;');
      lines.push('');
      lines.push(`export type ${symbolName} = (typeof ${symbolName})[keyof typeof ${symbolName}];`);
      lines.push('');
    } else {
      lines.push("import type { components } from '../schema.js';");
      lines.push('');
      lines.push(`export type ${symbolName} = components['schemas']['${escapeSingleQuoteString(schemaName)}'];`);
      lines.push('');
    }

    writeFileSync(filePath, lines.join('\n'), 'utf8');
  }

  process.stdout.write(`[generate:realm-sdk] generated models: ${schemaNames.length}\n`);
}
