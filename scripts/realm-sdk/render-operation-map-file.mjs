function buildOperationIndexes(operations) {
  const operationMap = {};
  const serviceMap = {};

  for (const operation of operations) {
    operationMap[operation.operationKey] = {
      operationId: operation.operationId,
      method: operation.method,
      path: operation.path,
      service: operation.service,
      methodName: operation.methodName,
      tag: operation.tag,
      parameters: operation.parameters,
      hasBody: operation.hasBody,
      bodyRequired: operation.bodyRequired,
      requestBodyContentType: operation.requestBodyContentType || undefined,
      successStatusCodes: operation.successStatusCodes,
      successContentTypes: operation.successContentTypes,
      hasSuccessBody: operation.hasSuccessBody,
    };

    if (!serviceMap[operation.service]) {
      serviceMap[operation.service] = {};
    }
    serviceMap[operation.service][operation.methodName] = operation.operationKey;
  }

  return [operationMap, serviceMap];
}

