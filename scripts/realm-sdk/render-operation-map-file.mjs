function buildOperationIndexes(operations) {
  const operationMap = {};
  const serviceMap = {};

  for (const operation of operations) {
    operationMap[operation.operationKey] = {
      operationId: operation.operationId,
      method: operation.method,
      path: operation.path,
      service: operation.service,
      methodName: operation.methodName,
      tag: operation.tag,
      parameters: operation.parameters,
      hasBody: operation.hasBody,
      bodyRequired: operation.bodyRequired,
      requestBodyContentType: operation.requestBodyContentType || undefined,
    };

    if (!serviceMap[operation.service]) {
      serviceMap[operation.service] = {};
    }
    serviceMap[operation.service][operation.methodName] = operation.operationKey;
  }

  return [operationMap, serviceMap];
}

export function renderOperationMapFile(operations) {
  const [operationMap, serviceMap] = buildOperationIndexes(operations);

  return [
    '/* eslint-disable */',
    '// AUTO-GENERATED by scripts/generate-realm-sdk.mjs. DO NOT EDIT.',
    '',
    "export type RealmHttpMethod = 'GET' | 'POST' | 'PUT' | 'PATCH' | 'DELETE' | 'OPTIONS' | 'HEAD';",
    "export type RealmOperationParameterIn = 'path' | 'query' | 'header' | 'cookie';",
    "export type RealmOperationValueType = 'string' | 'number' | 'boolean' | 'string[]' | 'number[]' | 'boolean[]' | 'unknown';",
    '',
    'export type RealmOperationParameter = {',
    '  name: string;',
    '  in: RealmOperationParameterIn;',
    '  required: boolean;',
    '  valueType: RealmOperationValueType;',
    '};',
    '',
    'export type RealmOperationDefinition = {',
    '  operationId: string;',
    '  method: RealmHttpMethod;',
    '  path: string;',
    '  service: string;',
    '  methodName: string;',
    '  tag: string;',
    '  parameters: readonly RealmOperationParameter[];',
    '  hasBody: boolean;',
    '  bodyRequired: boolean;',
    '  requestBodyContentType?: string;',
    '};',
    '',
    `export const REALM_OPERATION_MAP = ${JSON.stringify(operationMap, null, 2)} as const;`,
    '',
    `export const REALM_SERVICE_METHODS = ${JSON.stringify(serviceMap, null, 2)} as const;`,
    '',
    'export type RealmOperationMap = typeof REALM_OPERATION_MAP;',
    'export type RealmServiceMethodMap = typeof REALM_SERVICE_METHODS;',
    '',
  ].join('\n');
}
