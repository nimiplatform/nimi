import { existsSync, readdirSync, statSync, writeFileSync } from 'node:fs';
import path from 'node:path';
import { REALM_GENERATED_RELATIVE_PATH } from './constants.mjs';
import {
  asRecord,
  escapeSingleQuoteString,
  isValidTypeIdentifier,
  mergeOverlappingIdentifiers,
  reserveUniqueSymbol,
  stripSchemaSuffixes,
  toEnumMemberKey,
  toTypeIdentifier,
} from './model-utils.mjs';
import { normalizeEnumMemberKey } from './legacy-normalization.mjs';

export function writePropertyEnums(repoRoot, spec) {
  const generatedDir = path.join(repoRoot, REALM_GENERATED_RELATIVE_PATH);
  const outputPath = path.join(generatedDir, 'property-enums.ts');
  const modelsDir = path.join(generatedDir, 'models');
  const schemaMap = asRecord(asRecord(asRecord(spec).components).schemas);
  const schemaNames = Object.keys(schemaMap).sort((left, right) => left.localeCompare(right));
  const modelNames = existsSync(modelsDir) && statSync(modelsDir).isDirectory()
    ? readdirSync(modelsDir)
      .filter((entry) => entry.endsWith('.ts'))
      .map((entry) => entry.replace(/\.ts$/, ''))
    : [];
  const occupiedNames = new Set(modelNames);
  const entries = [];

  for (const schemaName of schemaNames) {
    const schema = asRecord(schemaMap[schemaName]);
    const properties = asRecord(schema.properties);
    const propertyNames = Object.keys(properties).sort((left, right) => left.localeCompare(right));
    const schemaSymbol = isValidTypeIdentifier(schemaName) ? schemaName : toTypeIdentifier(schemaName);
    const compactSchemaSymbol = stripSchemaSuffixes(schemaSymbol);

    for (const propertyName of propertyNames) {
      const propertySchema = asRecord(properties[propertyName]);
      const propertySymbol = toTypeIdentifier(propertyName);
      const inlineEnumValues = Array.isArray(propertySchema.enum) ? propertySchema.enum : null;
      const itemSchema = asRecord(propertySchema.items);
      const itemEnumValues = Array.isArray(itemSchema.enum) ? itemSchema.enum : null;
      const enumDefinitions = [];

      if (inlineEnumValues && inlineEnumValues.length > 0) {
        enumDefinitions.push({
          enumValues: inlineEnumValues,
          symbolTail: propertySymbol,
          typeExpression: `components['schemas']['${escapeSingleQuoteString(schemaName)}']['${escapeSingleQuoteString(propertyName)}']`,
        });
      }

      if (itemEnumValues && itemEnumValues.length > 0) {
        enumDefinitions.push({
          enumValues: itemEnumValues,
          symbolTail: `${propertySymbol}Item`,
          typeExpression: `(NonNullable<components['schemas']['${escapeSingleQuoteString(schemaName)}']['${escapeSingleQuoteString(propertyName)}']> extends ReadonlyArray<infer Item> ? Item : never)`,
        });
      }

      if (enumDefinitions.length === 0) {
        continue;
      }

      for (const enumDefinition of enumDefinitions) {
        const ergonomicName = mergeOverlappingIdentifiers(compactSchemaSymbol, enumDefinition.symbolTail);
        const canonicalName = `${schemaSymbol}${enumDefinition.symbolTail}`;
        const baseName = !occupiedNames.has(ergonomicName) && ergonomicName ? ergonomicName : canonicalName;
        const symbolName = reserveUniqueSymbol(baseName, occupiedNames);

        entries.push({
          symbolName,
          enumValues: enumDefinition.enumValues,
          typeExpression: enumDefinition.typeExpression,
        });
      }
    }
  }

  entries.sort((left, right) => left.symbolName.localeCompare(right.symbolName));

  const lines = [
    '/* eslint-disable */',
    '// AUTO-GENERATED by scripts/generate-realm-sdk.mjs. DO NOT EDIT.',
    '',
    "import type { components } from './schema.js';",
    '',
  ];

  for (const entry of entries) {
    lines.push(`export const ${entry.symbolName} = {`);
    const usedEnumMemberNames = new Map();
    for (let index = 0; index < entry.enumValues.length; index += 1) {
      const enumValue = entry.enumValues[index];
      const memberName = normalizeEnumMemberKey(toEnumMemberKey(enumValue, index, usedEnumMemberNames));
      lines.push(`  ${memberName}: ${JSON.stringify(enumValue)},`);
    }
    lines.push(
      `} as const satisfies Record<string, ${entry.typeExpression}>;`,
    );
    lines.push('');
    lines.push(`export type ${entry.symbolName} = (typeof ${entry.symbolName})[keyof typeof ${entry.symbolName}];`);
    lines.push('');
  }

  if (entries.length === 0) {
    lines.push('export {};');
    lines.push('');
  }

  writeFileSync(outputPath, lines.join('\n'), 'utf8');
  process.stdout.write(`[generate:realm-sdk] generated property enums: ${entries.length}\n`);
}
