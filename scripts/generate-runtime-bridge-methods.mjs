#!/usr/bin/env node

import { readFileSync, writeFileSync } from 'node:fs';
import { resolve } from 'node:path';

const repoRoot = resolve(new URL('..', import.meta.url).pathname);
const methodIdsFile = resolve(repoRoot, 'sdk/packages/runtime/src/method-ids.ts');
const outputFile = resolve(
  repoRoot,
  'apps/desktop/src-tauri/src/runtime_bridge/generated/method_ids.rs',
);

function parseRuntimeMethodMap(source) {
  const lines = source.split('\n');
  const map = new Map();

  let inRuntimeMethodIds = false;
  let currentService = '';

  for (const rawLine of lines) {
    const line = rawLine.trim();
    if (!inRuntimeMethodIds) {
      if (line.startsWith('export const RuntimeMethodIds = {')) {
        inRuntimeMethodIds = true;
      }
      continue;
    }

    if (line === '} as const;') {
      break;
    }

    const serviceMatch = line.match(/^([a-zA-Z0-9_]+): \{$/);
    if (serviceMatch) {
      currentService = serviceMatch[1];
      continue;
    }

    if (line === '},') {
      currentService = '';
      continue;
    }

    if (!currentService) {
      continue;
    }

    const methodMatch = line.match(
      /^([a-zA-Z0-9_]+): '([/][^']+)'[,]?$/,
    );
    if (!methodMatch) {
      continue;
    }
    const method = methodMatch[1];
    const path = methodMatch[2];
    map.set(`${currentService}.${method}`, path);
  }

  return map;
}

function parseStreamReferences(source) {
  const streamBlockMatch = source.match(
    /export const RuntimeStreamMethodIds:[\s\S]*?Object\.freeze\(\[([\s\S]*?)\]\);/,
  );
  if (!streamBlockMatch) {
    return [];
  }
  const block = streamBlockMatch[1] || '';
  const references = [];
  const regex = /RuntimeMethodIds\.([a-zA-Z0-9_]+)\.([a-zA-Z0-9_]+)/g;
  while (true) {
    const match = regex.exec(block);
    if (!match) {
      break;
    }
    references.push(`${match[1]}.${match[2]}`);
  }
  return references;
}

function uniqueSorted(values) {
  return Array.from(new Set(values)).sort();
}

function toRustConst(name, values) {
  const lines = values.map((value) => `    "${value}",`);
  return `pub const ${name}: &[&str] = &[\n${lines.join('\n')}\n];`;
}

function buildRustSource(allowlistedMethods, streamMethods) {
  return `// @generated by scripts/generate-runtime-bridge-methods.mjs
// DO NOT EDIT MANUALLY.

${toRustConst('RUNTIME_BRIDGE_ALLOWLISTED_METHODS', allowlistedMethods)}

${toRustConst('RUNTIME_BRIDGE_STREAM_METHODS', streamMethods)}

pub fn is_allowlisted_method(method_id: &str) -> bool {
    RUNTIME_BRIDGE_ALLOWLISTED_METHODS.contains(&method_id)
}

pub fn is_stream_method(method_id: &str) -> bool {
    RUNTIME_BRIDGE_STREAM_METHODS.contains(&method_id)
}
`;
}

function main() {
  const checkMode = process.argv.includes('--check');
  const source = readFileSync(methodIdsFile, 'utf8');
  const methodMap = parseRuntimeMethodMap(source);
  const allowlistedMethods = uniqueSorted(Array.from(methodMap.values()));

  const streamReferences = parseStreamReferences(source);
  const streamMethods = uniqueSorted(streamReferences.map((reference) => {
    const method = methodMap.get(reference);
    if (!method) {
      throw new Error(`failed to resolve stream method reference: ${reference}`);
    }
    return method;
  }));

  const unknownStreams = streamMethods.filter(
    (method) => !allowlistedMethods.includes(method),
  );
  if (unknownStreams.length > 0) {
    throw new Error(`stream methods not in allowlist: ${unknownStreams.join(', ')}`);
  }

  const rendered = buildRustSource(allowlistedMethods, streamMethods);
  if (checkMode) {
    let current = '';
    try {
      current = readFileSync(outputFile, 'utf8');
    } catch {
      process.stderr.write(
        `runtime bridge method file missing: ${outputFile}\n` +
        'run `pnpm generate:runtime-bridge-methods` to regenerate.\n',
      );
      process.exitCode = 1;
      return;
    }
    if (current !== rendered) {
      process.stderr.write(
        `runtime bridge method drift detected: ${outputFile}\n` +
        'run `pnpm generate:runtime-bridge-methods` to regenerate.\n',
      );
      process.exitCode = 1;
      return;
    }
    process.stdout.write(`up-to-date ${outputFile}\n`);
    return;
  }

  writeFileSync(outputFile, rendered, 'utf8');
  process.stdout.write(`generated ${outputFile}\n`);
}

main();
