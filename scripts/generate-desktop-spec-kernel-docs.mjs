function normalizeMarkdown(markdown) {
  return `${markdown.replace(/\n{3,}/gu, '\n\n').replace(/\n+$/u, '\n')}`;
}

function header(title, sourceName) {
  return normalizeMarkdown([
    `# ${title}`,
    '',
    '> AUTO-GENERATED by `scripts/generate-desktop-spec-kernel-docs.mjs`. DO NOT EDIT.',
    `> Source: \`spec/desktop/kernel/tables/${sourceName}\``,
    '',
  ].join('\n'));
}

function mdBool(value) {
  return value ? 'true' : 'false';
}

function renderBootstrapPhases(doc, sourceName) {
  const phases = Array.isArray(doc?.phases) ? doc.phases : [];
  const constraints = Array.isArray(doc?.constraints) ? doc.constraints : [];
  let out = header('Generated Bootstrap Phases', sourceName);

  out += '| Phase | Order | Async | Gated By | Source Rule |\n';
  out += '|---|---:|---|---|---|\n';
  for (const item of phases) {
    const phase = String(item?.phase || '').trim();
    if (!phase) continue;
    const order = Number(item?.order);
    const async = mdBool(Boolean(item?.async));
    const gatedBy = String(item?.gated_by || '').trim() || '—';
    const sourceRule = String(item?.source_rule || '').trim() || '—';
    out += `| \`${phase}\` | ${order} | \`${async}\` | \`${gatedBy}\` | \`${sourceRule}\` |\n`;
  }
  out += '\n';

  if (constraints.length > 0) {
    out += '## Cross-Cutting Constraints\n\n';
    out += '| Constraint | Description | Source Rule |\n';
    out += '|---|---|---|\n';
    for (const item of constraints) {
      const constraint = String(item?.constraint || '').trim();
      if (!constraint) continue;
      const description = String(item?.description || '').trim() || '—';
      const sourceRule = String(item?.source_rule || '').trim() || '—';
      out += `| \`${constraint}\` | ${description} | \`${sourceRule}\` |\n`;
    }
    out += '\n';
  }

  return normalizeMarkdown(out);
}

