#!/usr/bin/env node

import { promises as fs } from 'node:fs';
import path from 'node:path';
import { fileURLToPath } from 'node:url';
import YAML from 'yaml';

const scriptDir = path.dirname(fileURLToPath(import.meta.url));
const repoRoot = path.resolve(scriptDir, '..');
const tablePath = path.join(repoRoot, '.nimi', 'spec', 'runtime', 'kernel', 'tables', 'connector-auth-profiles.yaml');
const runtimeOutPath = path.join(repoRoot, 'runtime', 'internal', 'services', 'connector', 'auth_profiles.generated.go');
const sdkOutPath = path.join(repoRoot, 'sdk', 'src', 'runtime', 'connector-auth-profiles.generated.ts');

const allowedHeaderBehaviors = new Set(['none', 'codex_oauth', 'anthropic']);

function normalizeString(value) {
  return String(value || '').trim();
}

function normalizeLower(value) {
  return normalizeString(value).toLowerCase();
}

function normalizeStringArray(value, fieldName, profileID) {
  if (!Array.isArray(value) || value.length === 0) {
    throw new Error(`profile ${profileID} must define non-empty ${fieldName}`);
  }
  const seen = new Set();
  const output = [];
  for (const item of value) {
    const normalized = normalizeLower(item);
    if (!normalized || seen.has(normalized)) {
      continue;
    }
    seen.add(normalized);
    output.push(normalized);
  }
  if (output.length === 0) {
    throw new Error(`profile ${profileID} must define non-empty ${fieldName}`);
  }
  return output;
}

function parseProfiles(raw) {
  const profiles = Array.isArray(raw?.profiles) ? raw.profiles : [];
  const seenIDs = new Set();
  return profiles.map((entry) => {
    const id = normalizeLower(entry?.id);
    if (!id) {
      throw new Error('connector-auth-profiles.yaml contains profile with empty id');
    }
    if (seenIDs.has(id)) {
      throw new Error(`connector-auth-profiles.yaml duplicates profile id ${id}`);
    }
    seenIDs.add(id);
    const authKind = normalizeLower(entry?.auth_kind);
    if (authKind !== 'oauth_managed') {
      throw new Error(`profile ${id} must use auth_kind=oauth_managed`);
    }
    const headerBehavior = normalizeLower(entry?.header_behavior) || 'none';
    if (!allowedHeaderBehaviors.has(headerBehavior)) {
      throw new Error(`profile ${id} uses unsupported header_behavior ${headerBehavior}`);
    }
    return {
      id,
      authKind,
      allowedProviders: normalizeStringArray(entry?.allowed_providers, 'allowed_providers', id),
      headerBehavior,
      sourceRule: normalizeString(entry?.source_rule) || '—',
    };
  }).sort((a, b) => a.id.localeCompare(b.id));
}

function quoteGo(value) {
  return JSON.stringify(String(value));
}

function quoteTS(value) {
  return JSON.stringify(String(value));
}

function renderGo(profiles) {
  const records = profiles.map((profile) => {
    const providers = profile.allowedProviders.map((provider) => `\t\t\t${quoteGo(provider)},`).join('\n');
    return `\t${quoteGo(profile.id)}: {\n` +
      `\t\tID: ${quoteGo(profile.id)},\n` +
      `\t\tAuthKind: ${quoteGo(profile.authKind)},\n` +
      `\t\tAllowedProviders: []string{\n${providers}\n\t\t},\n` +
      `\t\tHeaderBehavior: ${quoteGo(profile.headerBehavior)},\n` +
      `\t\tSourceRule: ${quoteGo(profile.sourceRule)},\n` +
      `\t},`;
  }).join('\n');

  return `// Code generated by scripts/generate-runtime-connector-auth-profiles.mjs. DO NOT EDIT.\n\n` +
    `package connector\n\n` +
    `type ProviderAuthProfileSpec struct {\n` +
    `\tID string\n` +
    `\tAuthKind string\n` +
    `\tAllowedProviders []string\n` +
    `\tHeaderBehavior string\n` +
    `\tSourceRule string\n` +
    `}\n\n` +
    `var GeneratedProviderAuthProfiles = map[string]ProviderAuthProfileSpec{\n${records}\n}\n`;
}

function renderTS(profiles) {
  const records = profiles.map((profile) => {
    const providers = profile.allowedProviders.map((provider) => quoteTS(provider)).join(', ');
    return `  ${quoteTS(profile.id)}: {\n` +
      `    id: ${quoteTS(profile.id)},\n` +
      `    authKind: ${quoteTS(profile.authKind)},\n` +
      `    allowedProviders: [${providers}],\n` +
      `    headerBehavior: ${quoteTS(profile.headerBehavior)},\n` +
      `    sourceRule: ${quoteTS(profile.sourceRule)},\n` +
      `  },`;
  }).join('\n');

  return `// Code generated by scripts/generate-runtime-connector-auth-profiles.mjs. DO NOT EDIT.\n\n` +
    `export type ConnectorAuthProfileSpec = {\n` +
    `  id: string;\n` +
    `  authKind: 'oauth_managed';\n` +
    `  allowedProviders: readonly string[];\n` +
    `  headerBehavior: 'none' | 'codex_oauth' | 'anthropic';\n` +
    `  sourceRule: string;\n` +
    `};\n\n` +
    `export const CONNECTOR_AUTH_PROFILES: Record<string, ConnectorAuthProfileSpec> = {\n${records}\n};\n`;
}

async function main() {
  const check = process.argv.includes('--check');
  const raw = YAML.parse(await fs.readFile(tablePath, 'utf8'));
  const profiles = parseProfiles(raw);
  const runtimeOut = renderGo(profiles);
  const sdkOut = renderTS(profiles);

  if (check) {
    const currentRuntime = await fs.readFile(runtimeOutPath, 'utf8');
    const currentSDK = await fs.readFile(sdkOutPath, 'utf8');
    if (currentRuntime !== runtimeOut || currentSDK !== sdkOut) {
      throw new Error('connector auth profile generated files are out of date');
    }
    return;
  }

  await fs.writeFile(runtimeOutPath, runtimeOut);
  await fs.writeFile(sdkOutPath, sdkOut);
}

main().catch((error) => {
  process.stderr.write(`generate-runtime-connector-auth-profiles failed: ${String(error)}\n`);
  process.exit(1);
});
