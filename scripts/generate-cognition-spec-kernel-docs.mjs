function normalizeMarkdown(markdown) {
  return `${markdown.replace(/\n{3,}/gu, '\n\n').replace(/\n+$/u, '\n')}`;
}

function header(title, sourceName) {
  return normalizeMarkdown([
    `# ${title}`,
    '',
    '> AUTO-GENERATED by `scripts/generate-cognition-spec-kernel-docs.mjs`. DO NOT EDIT.',
    `> Source: \`.nimi/spec/cognition/kernel/tables/${sourceName}\``,
    '',
  ].join('\n'));
}

function renderRuleEvidence(doc, sourceName) {
  const compliance = doc?.rule_compliance ?? {};
  const rules = Array.isArray(doc?.rules) ? doc.rules : [];
  let out = header('Generated Cognition Rule Evidence', sourceName);

  out += '| Metric | Value |\n';
  out += '|---|---|\n';
  out += `| \`total_c_rules\` | ${String(compliance?.total_c_rules ?? '—')} |\n`;
  out += `| \`audit_date\` | ${String(compliance?.audit_date ?? '—')} |\n`;
  out += '\n';

  out += '## Evidence Catalog\n\n';
  out += '| Evidence Ref | Type | Command | Path | Description |\n';
  out += '|---|---|---|---|---|\n';
  const catalog = doc?.evidence_catalog && typeof doc.evidence_catalog === 'object'
    ? Object.entries(doc.evidence_catalog)
    : [];
  for (const [ref, entry] of catalog) {
    const type = String(entry?.type || '').trim() || '—';
    const command = String(entry?.command || '').trim() || '—';
    const relPath = String(entry?.path || '').trim() || '—';
    const description = String(entry?.description || '').trim() || '—';
    out += `| \`${ref}\` | \`${type}\` | \`${command}\` | \`${relPath}\` | ${description} |\n`;
  }
  out += '\n';

  out += '## Rule Coverage\n\n';
  out += '| Rule ID | Status | Evidence Refs | Note |\n';
  out += '|---|---|---|---|\n';
  for (const rule of rules) {
    const ruleID = String(rule?.rule_id || '').trim();
    if (!ruleID) continue;
    const status = String(rule?.status || '').trim() || 'unknown';
    const refs = Array.isArray(rule?.evidence_refs)
      ? rule.evidence_refs.map((value) => `\`${String(value)}\``).join(', ')
      : '—';
    const note = String(rule?.note || '').trim() || '—';
    out += `| \`${ruleID}\` | \`${status}\` | ${refs} | ${note} |\n`;
  }
  out += '\n';

  return normalizeMarkdown(out);
}

