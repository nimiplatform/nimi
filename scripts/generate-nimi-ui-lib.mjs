#!/usr/bin/env node

import { promises as fs } from 'node:fs';
import path from 'node:path';
import { fileURLToPath } from 'node:url';
import YAML from 'yaml';

const scriptDir = path.dirname(fileURLToPath(import.meta.url));
const repoRoot = path.resolve(scriptDir, '..');
const tablesDir = path.join(repoRoot, 'spec', 'platform', 'kernel', 'tables');
const outDir = path.join(repoRoot, 'kit', 'ui', 'src', 'generated');
const themesDir = path.join(outDir, 'themes');

const DESKTOP_ACCENT_PALETTE = {
  mint: {
    50: '#e6f7f2',
    100: '#ccefe5',
    200: '#99dfc8',
    300: '#66cfab',
    400: '#33bf8e',
    500: '#4ECCA3',
    600: '#3DBB96',
    700: '#2AA880',
    800: '#1F8C6B',
    900: '#166B52',
  },
};
const LEGACY_GENERATED_THEME_FILES = [
  path.join(themesDir, 'relay-dark.css'),
  path.join(themesDir, 'overtone-studio.css'),
];

async function readYaml(fileName) {
  const raw = await fs.readFile(path.join(tablesDir, fileName), 'utf8');
  return YAML.parse(raw);
}

function cssHeader(source) {
  return [
    '/* AUTO-GENERATED by scripts/generate-nimi-ui-lib.mjs. DO NOT EDIT. */',
    `/* Source: spec/platform/kernel/tables/${source} */`,
    '',
  ].join('\n');
}

function toJs(value) {
  return JSON.stringify(value, null, 2);
}

function styleObjectToCss(styles) {
  return Object.entries(styles || {})
    .map(([prop, value]) => `  ${prop}: ${value};`)
    .join('\n');
}

function normalizePacks(themesDoc) {
  return Array.isArray(themesDoc?.packs) ? themesDoc.packs : [];
}

function tokenRows(tokensDoc) {
  return Array.isArray(tokensDoc?.tokens) ? tokensDoc.tokens : [];
}

function primitiveRows(primitivesDoc) {
  return Array.isArray(primitivesDoc?.primitives) ? primitivesDoc.primitives : [];
}

function packValueMap(pack) {
  const values = pack?.values && typeof pack.values === 'object' ? pack.values : {};
  return new Map(Object.entries(values).map(([k, v]) => [String(k), String(v)]));
}

function renderTokensFile(tokensDoc, themesDoc) {
  const tokens = tokenRows(tokensDoc);
  const packs = normalizePacks(themesDoc);
  const tokenDefs = {};
  const categoryGroups = {};
  const typographyRoles = [];

  for (const token of tokens) {
    const id = String(token.id);
    tokenDefs[id] = {
      category: String(token.category),
      cssVar: String(token.css_var),
      semanticName: String(token.semantic_name),
      primitiveFamily: String(token.primitive_family),
      themeLayer: String(token.theme_layer || 'foundation'),
      tsToken: String(token.ts_token),
    };
    const category = String(token.category);
    categoryGroups[category] = categoryGroups[category] || [];
    categoryGroups[category].push(id);
    const typographyMatch = id.match(/^typography\.([a-z_]+)\.size$/u);
    if (typographyMatch && !typographyRoles.includes(typographyMatch[1])) {
      typographyRoles.push(typographyMatch[1]);
    }
  }

  const foundationSchemeIds = packs.filter((pack) => String(pack?.pack_kind) === 'foundation').map((pack) => String(pack.theme_id));
  const accentPackIds = packs.filter((pack) => String(pack?.pack_kind) === 'accent').map((pack) => String(pack.theme_id));
  const packIds = packs.map((pack) => String(pack.theme_id));

  return [
    '// AUTO-GENERATED by scripts/generate-nimi-ui-lib.mjs. DO NOT EDIT.',
    '// Source: spec/platform/kernel/tables/nimi-ui-tokens.yaml + spec/platform/kernel/tables/nimi-ui-themes.yaml',
    '',
    `export const DESIGN_PACK_IDS = ${toJs(packIds)} as const;`,
    `export const FOUNDATION_SCHEME_IDS = ${toJs(foundationSchemeIds)} as const;`,
    `export const ACCENT_PACK_IDS = ${toJs(accentPackIds)} as const;`,
    `export const TOKEN_DEFINITIONS = ${toJs(tokenDefs)} as const;`,
    `export const TOKEN_CATEGORY_GROUPS = ${toJs(categoryGroups)} as const;`,
    `export const TYPOGRAPHY_ROLE_IDS = ${toJs(typographyRoles)} as const;`,
    '',
    'export type GeneratedNimiDesignPackId = (typeof DESIGN_PACK_IDS)[number];',
    'export type GeneratedNimiFoundationSchemeId = (typeof FOUNDATION_SCHEME_IDS)[number];',
    'export type GeneratedNimiAccentPackId = (typeof ACCENT_PACK_IDS)[number];',
    'export type GeneratedNimiTokenId = keyof typeof TOKEN_DEFINITIONS;',
    'export type GeneratedNimiTypographyRoleId = (typeof TYPOGRAPHY_ROLE_IDS)[number];',
    '',
  ].join('\n');
}

function collectClassNames(groupEntries = []) {
  return Object.fromEntries(
    groupEntries
      .map((entry) => [String(entry?.id || ''), String(entry?.class_name || '')])
      .filter(([id, className]) => id && className),
  );
}

function collectSlotClassNames(slots = []) {
  return Object.fromEntries(
    slots
      .map((slot) => [String(slot?.id || ''), String(slot?.class_name || '')])
      .filter(([id, className]) => id && className),
  );
}

function renderPrimitiveContractFile(primitivesDoc, tokensDoc) {
  const primitives = primitiveRows(primitivesDoc);
  const byId = Object.fromEntries(primitives.map((primitive) => [String(primitive.id), primitive]));
  const typographyRoles = tokenRows(tokensDoc)
    .map((token) => String(token?.id || '').match(/^typography\.([a-z_]+)\.size$/u)?.[1] || '')
    .filter(Boolean)
    .filter((role, index, all) => all.indexOf(role) === index);

  const surface = byId['primitive.surface'] || {};
  const action = byId['primitive.action'] || {};
  const overlay = byId['primitive.overlay'] || {};
  const sidebar = byId['primitive.sidebar'] || {};
  const field = byId['primitive.field'] || {};
  const status = byId['primitive.status'] || {};

  const typographyRoleClass = Object.fromEntries(
    typographyRoles.map((role) => [
      role.replace(/_([a-z])/gu, (_, chr) => chr.toUpperCase()),
      `nimi-type--${role.replace(/_/gu, '-')}`,
    ]),
  );

  return [
    '// AUTO-GENERATED by scripts/generate-nimi-ui-lib.mjs. DO NOT EDIT.',
    '// Source: spec/platform/kernel/tables/nimi-ui-primitives.yaml + spec/platform/kernel/tables/nimi-ui-tokens.yaml',
    '',
    `export const PRIMITIVE_CONTRACT = ${toJs(byId)} as const;`,
    `export const SURFACE_TONE_CLASS = ${toJs(collectClassNames(surface?.class_groups?.tone))} as const;`,
    `export const SURFACE_ELEVATION_CLASS = ${toJs(collectClassNames(surface?.class_groups?.elevation))} as const;`,
    `export const ACTION_TONE_CLASS = ${toJs(collectClassNames(action?.class_groups?.tone))} as const;`,
    `export const ACTION_SIZE_CLASS = ${toJs(collectClassNames(action?.class_groups?.size))} as const;`,
    `export const ACTION_SLOT_CLASS = ${toJs(collectSlotClassNames(action?.slots))} as const;`,
    `export const OVERLAY_BACKDROP_CLASS = ${toJs(collectClassNames(overlay?.class_groups?.kind_backdrop))} as const;`,
    `export const OVERLAY_PANEL_CLASS = ${toJs(collectClassNames(overlay?.class_groups?.kind_panel))} as const;`,
    `export const OVERLAY_SLOT_CLASS = ${toJs(collectSlotClassNames(overlay?.slots))} as const;`,
    `export const SIDEBAR_FAMILY_CLASS = ${toJs(collectClassNames(sidebar?.class_groups?.family))} as const;`,
    `export const SIDEBAR_ITEM_KIND_CLASS = ${toJs(collectClassNames(sidebar?.class_groups?.item_kind))} as const;`,
    `export const SIDEBAR_AFFORDANCE_CLASS = ${toJs(collectClassNames(sidebar?.class_groups?.affordance))} as const;`,
    `export const SIDEBAR_SLOT_CLASS = ${toJs(collectSlotClassNames(sidebar?.slots))} as const;`,
    `export const FIELD_TONE_CLASS = ${toJs(collectClassNames(field?.class_groups?.tone))} as const;`,
    `export const FIELD_SLOT_CLASS = ${toJs(collectSlotClassNames(field?.slots))} as const;`,
    `export const STATUS_TONE_CLASS = ${toJs(collectClassNames(status?.class_groups?.tone))} as const;`,
    `export const TYPOGRAPHY_TOKEN_CLASS = ${toJs(typographyRoleClass)} as const;`,
    '',
    'export type GeneratedSurfaceTone = keyof typeof SURFACE_TONE_CLASS;',
    'export type GeneratedSurfaceElevation = keyof typeof SURFACE_ELEVATION_CLASS;',
    'export type GeneratedActionTone = keyof typeof ACTION_TONE_CLASS;',
    'export type GeneratedActionSize = keyof typeof ACTION_SIZE_CLASS;',
    'export type GeneratedOverlayKind = keyof typeof OVERLAY_PANEL_CLASS;',
    'export type GeneratedSidebarFamily = keyof typeof SIDEBAR_FAMILY_CLASS;',
    'export type GeneratedSidebarItemKind = keyof typeof SIDEBAR_ITEM_KIND_CLASS;',
    'export type GeneratedSidebarAffordance = keyof typeof SIDEBAR_AFFORDANCE_CLASS;',
    'export type GeneratedFieldTone = keyof typeof FIELD_TONE_CLASS;',
    'export type GeneratedStatusTone = keyof typeof STATUS_TONE_CLASS;',
    'export type GeneratedTypographyToken = keyof typeof TYPOGRAPHY_TOKEN_CLASS;',
    '',
  ].join('\n');
}

function selectorForEntry(entry) {
  if (entry?.selector) {
    return String(entry.selector);
  }
  if (entry?.class_name) {
    return `.${String(entry.class_name)}`;
  }
  return '';
}

function renderCssRule(entry) {
  const selector = selectorForEntry(entry);
  const css = styleObjectToCss(entry?.styles || {});
  if (!selector || !css) {
    return '';
  }
  return `${selector} {\n${css}\n}\n`;
}

function renderPrimitiveCss(primitivesDoc) {
  const blocks = [cssHeader('nimi-ui-primitives.yaml')];
  for (const primitive of primitiveRows(primitivesDoc)) {
    for (const slot of Array.isArray(primitive?.slots) ? primitive.slots : []) {
      const css = renderCssRule(slot);
      if (css) blocks.push(css);
    }
    const classGroups = primitive?.class_groups && typeof primitive.class_groups === 'object' ? primitive.class_groups : {};
    for (const entries of Object.values(classGroups)) {
      if (!Array.isArray(entries)) continue;
      for (const entry of entries) {
        const css = renderCssRule(entry);
        if (css) blocks.push(css);
      }
    }
  }
  blocks.push('@media (prefers-reduced-motion: reduce) {');
  blocks.push('  .nimi-surface,');
  blocks.push('  .nimi-action,');
  blocks.push('  .nimi-sidebar-item,');
  blocks.push('  .nimi-field,');
  blocks.push('  .nimi-overlay-backdrop,');
  blocks.push('  .nimi-overlay-panel,');
  blocks.push('  .nimi-tooltip-layer {');
  blocks.push('    transition-duration: 0ms !important;');
  blocks.push('    animation-duration: 0ms !important;');
  blocks.push('  }');
  blocks.push('}');
  blocks.push('');
  return `${blocks.join('\n').trimEnd()}\n`;
}

function toKebab(role) {
  return role.replace(/_/gu, '-');
}

function toCamel(role) {
  return role.replace(/_([a-z])/gu, (_, chr) => chr.toUpperCase());
}

function renderTypographyCss(tokensDoc) {
  const roleSet = new Set();
  for (const token of tokenRows(tokensDoc)) {
    const match = String(token?.id || '').match(/^typography\.([a-z_]+)\.size$/u);
    if (match) roleSet.add(match[1]);
  }
  const blocks = [cssHeader('nimi-ui-tokens.yaml')];
  for (const role of [...roleSet].sort()) {
    const cssRole = toKebab(role);
    blocks.push(`.nimi-type--${cssRole} {`);
    blocks.push(`  font-size: var(--nimi-type-${cssRole}-size);`);
    blocks.push(`  line-height: var(--nimi-type-${cssRole}-line-height);`);
    blocks.push(`  font-weight: var(--nimi-type-${cssRole}-weight);`);
    blocks.push(`  letter-spacing: var(--nimi-type-${cssRole}-letter-spacing);`);
    if (role === 'mono') {
      blocks.push('  font-family: var(--nimi-font-mono);');
    }
    blocks.push('}\n');
  }
  return `${blocks.join('\n').trimEnd()}\n`;
}

function renderPackSelector(tokensDoc, pack, selector, onlyLayer = null) {
  const values = packValueMap(pack);
  const lines = [cssHeader('nimi-ui-themes.yaml'), `${selector} {`];
  for (const token of tokenRows(tokensDoc)) {
    const themeLayer = String(token?.theme_layer || 'foundation');
    if (onlyLayer && themeLayer !== onlyLayer) continue;
    const id = String(token.id);
    const cssVar = String(token.css_var);
    const value = values.get(id);
    if (!value) continue;
    lines.push(`  ${cssVar}: ${value};`);
  }
  lines.push('}');
  lines.push('');
  return lines.join('\n');
}

function renderFoundationTheme(tokensDoc, themesDoc, themeId, selector) {
  const pack = normalizePacks(themesDoc).find((item) => String(item?.theme_id) === themeId);
  if (!pack) throw new Error(`missing pack ${themeId}`);
  return renderPackSelector(tokensDoc, pack, selector, 'foundation');
}

function renderAccentTheme(tokensDoc, themesDoc, themeId) {
  const pack = normalizePacks(themesDoc).find((item) => String(item?.theme_id) === themeId);
  if (!pack) throw new Error(`missing pack ${themeId}`);
  const selector = `:root[data-nimi-accent="${themeId}"]`;
  return renderPackSelector(tokensDoc, pack, selector, 'accent');
}

function renderDesktopAccent(themeId, tokensDoc, themesDoc) {
  const selector = `:root[data-nimi-accent="${themeId}"]`;
  const lines = [renderAccentTheme(tokensDoc, themesDoc, themeId).trimEnd(), `${selector} {`];
  lines.push('  --font-ui: var(--nimi-font-sans);');
  lines.push('  --font-display: "Noto Sans SC", "Source Han Sans SC", "Inter", "PingFang SC", "Microsoft YaHei UI", "Segoe UI", system-ui, sans-serif;');
  lines.push('  --font-serif: "Noto Serif SC", "Source Han Serif SC", "Songti SC", "STSong", Georgia, serif;');
  lines.push('}');
  lines.push('');
  lines.push('@theme {');
  for (const [name, scale] of Object.entries(DESKTOP_ACCENT_PALETTE)) {
    for (const [step, value] of Object.entries(scale)) {
      lines.push(`  --color-${name}-${step}: ${value};`);
    }
  }
  lines.push('}');
  lines.push('');
  return lines.join('\n');
}

function renderRelayAccent(tokensDoc, themesDoc) {
  const selector = ':root[data-nimi-accent="relay-accent"]';
  return [
    renderAccentTheme(tokensDoc, themesDoc, 'relay-accent').trimEnd(),
    `${selector} {`,
    '  --nimi-accent-glow: 0 0 20px rgba(124, 111, 255, 0.15);',
    '}',
    '',
  ].join('\n');
}

function renderOvertoneAccent(tokensDoc, themesDoc) {
  const selector = ':root[data-nimi-accent="overtone-accent"]';
  return [
    renderAccentTheme(tokensDoc, themesDoc, 'overtone-accent').trimEnd(),
    `${selector} {`,
    '  --nimi-accent-glow: 0 0 20px rgba(139, 92, 246, 0.35), 0 0 60px rgba(139, 92, 246, 0.15);',
    '}',
    '',
  ].join('\n');
}

async function main() {
  const checkMode = process.argv.includes('--check');
  const tokensDoc = await readYaml('nimi-ui-tokens.yaml');
  const primitivesDoc = await readYaml('nimi-ui-primitives.yaml');
  const themesDoc = await readYaml('nimi-ui-themes.yaml');

  const rendered = new Map([
    [path.join(outDir, 'tokens.ts'), renderTokensFile(tokensDoc, themesDoc)],
    [path.join(outDir, 'primitive-contract.ts'), renderPrimitiveContractFile(primitivesDoc, tokensDoc)],
    [path.join(outDir, 'primitives.css'), renderPrimitiveCss(primitivesDoc)],
    [path.join(outDir, 'typography.css'), renderTypographyCss(tokensDoc)],
    [path.join(themesDir, 'light.css'), renderFoundationTheme(tokensDoc, themesDoc, 'nimi-light', ':root:not([data-nimi-scheme]), :root[data-nimi-scheme="light"]')],
    [path.join(themesDir, 'dark.css'), renderFoundationTheme(tokensDoc, themesDoc, 'nimi-dark', ':root[data-nimi-scheme="dark"]')],
    [path.join(themesDir, 'desktop-accent.css'), renderDesktopAccent('desktop-accent', tokensDoc, themesDoc)],
    [path.join(themesDir, 'forge-accent.css'), renderDesktopAccent('forge-accent', tokensDoc, themesDoc)],
    [path.join(themesDir, 'relay-accent.css'), renderRelayAccent(tokensDoc, themesDoc)],
    [path.join(themesDir, 'overtone-accent.css'), renderOvertoneAccent(tokensDoc, themesDoc)],
  ]);

  if (checkMode) {
    const drifted = [];
    for (const [filePath, contents] of rendered) {
      let current = '';
      try {
        current = await fs.readFile(filePath, 'utf8');
      } catch {
        drifted.push(filePath);
        continue;
      }
      if (current !== contents) {
        drifted.push(filePath);
      }
    }
    if (drifted.length > 0) {
      process.stderr.write('nimi ui lib drift detected:\n');
      for (const item of drifted) {
        process.stderr.write(`  - ${path.relative(repoRoot, item)}\n`);
      }
      process.stderr.write('run `pnpm generate:nimi-ui-lib` to regenerate.\n');
      process.exitCode = 1;
      return;
    }
    const staleResolved = [];
    for (const filePath of LEGACY_GENERATED_THEME_FILES) {
      try {
        await fs.access(filePath);
        staleResolved.push(filePath);
      } catch {
        // already absent
      }
    }
    if (staleResolved.length > 0) {
      process.stderr.write('nimi ui lib contains legacy generated themes:\n');
      for (const item of staleResolved) {
        process.stderr.write(`  - ${path.relative(repoRoot, item)}\n`);
      }
      process.stderr.write('run `pnpm generate:nimi-ui-lib` to clean legacy outputs.\n');
      process.exitCode = 1;
      return;
    }
    process.stdout.write(`nimi ui lib is up-to-date (${rendered.size} files)\n`);
    return;
  }

  await fs.mkdir(themesDir, { recursive: true });
  for (const legacyFile of LEGACY_GENERATED_THEME_FILES) {
    await fs.rm(legacyFile, { force: true });
  }
  for (const [filePath, contents] of rendered) {
    await fs.mkdir(path.dirname(filePath), { recursive: true });
    await fs.writeFile(filePath, contents, 'utf8');
  }
  process.stdout.write(`generated nimi ui lib projection (${rendered.size} files)\n`);
}

main().catch((error) => {
  process.stderr.write(`generate-nimi-ui-lib failed: ${String(error)}\n`);
  process.exitCode = 1;
});
