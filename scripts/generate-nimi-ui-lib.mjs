#!/usr/bin/env node

import { promises as fs } from 'node:fs';
import path from 'node:path';
import { fileURLToPath } from 'node:url';
import YAML from 'yaml';

const scriptDir = path.dirname(fileURLToPath(import.meta.url));
const repoRoot = path.resolve(scriptDir, '..');
const tablesDir = path.join(repoRoot, '.nimi', 'spec', 'platform', 'kernel', 'tables');
const outDir = path.join(repoRoot, 'kit', 'ui', 'src', 'generated');
const themesDir = path.join(outDir, 'themes');

const DESKTOP_ACCENT_PALETTE = {
  mint: {
    50: '#e6f7f2',
    100: '#ccefe5',
    200: '#99dfc8',
    300: '#66cfab',
    400: '#33bf8e',
    500: '#4ECCA3',
    600: '#3DBB96',
    700: '#2AA880',
    800: '#1F8C6B',
    900: '#166B52',
  },
};

const LEGACY_GENERATED_FILES = [
  path.join(themesDir, 'relay-dark.css'),
  path.join(themesDir, 'overtone-studio.css'),
  path.join(outDir, 'primitive-contract.ts'),
  path.join(outDir, 'primitives.css'),
  path.join(outDir, 'typography.css'),
];

async function readYaml(fileName) {
  const raw = await fs.readFile(path.join(tablesDir, fileName), 'utf8');
  return YAML.parse(raw);
}

function cssHeader(source) {
  return [
    '/* AUTO-GENERATED by scripts/generate-nimi-ui-lib.mjs. DO NOT EDIT. */',
    `/* Source: .nimi/spec/platform/kernel/tables/${source} */`,
    '',
  ].join('\n');
}

function toJs(value) {
  return JSON.stringify(value, null, 2);
}

function normalizePacks(themesDoc) {
  return Array.isArray(themesDoc?.packs) ? themesDoc.packs : [];
}

function tokenRows(tokensDoc) {
  return Array.isArray(tokensDoc?.tokens) ? tokensDoc.tokens : [];
}

function packValueMap(pack) {
  const values = pack?.values && typeof pack.values === 'object' ? pack.values : {};
  return new Map(Object.entries(values).map(([k, v]) => [String(k), String(v)]));
}

function toKebab(role) {
  return role.replace(/_/gu, '-');
}

// ---------------------------------------------------------------------------
// tokens.ts — simplified: pack/scheme/accent IDs + types only
// ---------------------------------------------------------------------------

function renderTokensFile(tokensDoc, themesDoc) {
  const packs = normalizePacks(themesDoc);
  const tokens = tokenRows(tokensDoc);

  const typographyRoles = [];
  for (const token of tokens) {
    const match = String(token?.id || '').match(/^typography\.([a-z_]+)\.size$/u);
    if (match && !typographyRoles.includes(match[1])) {
      typographyRoles.push(match[1]);
    }
  }

  const foundationSchemeIds = packs.filter((p) => String(p?.pack_kind) === 'foundation').map((p) => String(p.theme_id));
  const accentPackIds = packs.filter((p) => String(p?.pack_kind) === 'accent').map((p) => String(p.theme_id));
  const packIds = packs.map((p) => String(p.theme_id));

  return [
    '// AUTO-GENERATED by scripts/generate-nimi-ui-lib.mjs. DO NOT EDIT.',
    '// Source: .nimi/spec/platform/kernel/tables/nimi-ui-tokens.yaml + .nimi/spec/platform/kernel/tables/nimi-ui-themes.yaml',
    '',
    `export const DESIGN_PACK_IDS = ${toJs(packIds)} as const;`,
    `export const FOUNDATION_SCHEME_IDS = ${toJs(foundationSchemeIds)} as const;`,
    `export const ACCENT_PACK_IDS = ${toJs(accentPackIds)} as const;`,
    `export const TYPOGRAPHY_ROLE_IDS = ${toJs(typographyRoles)} as const;`,
    '',
    'export type GeneratedNimiDesignPackId = (typeof DESIGN_PACK_IDS)[number];',
    'export type GeneratedNimiFoundationSchemeId = (typeof FOUNDATION_SCHEME_IDS)[number];',
    'export type GeneratedNimiAccentPackId = (typeof ACCENT_PACK_IDS)[number];',
    'export type GeneratedNimiTypographyRoleId = (typeof TYPOGRAPHY_ROLE_IDS)[number];',
    '',
  ].join('\n');
}

// ---------------------------------------------------------------------------
// theme-base.css — @theme registration + @utility typography classes
// ---------------------------------------------------------------------------

function renderThemeBase(tokensDoc) {
  const tokens = tokenRows(tokensDoc);
  const blocks = [cssHeader('nimi-ui-tokens.yaml')];

  // Register all CSS vars with Tailwind 4 @theme so they become valid theme tokens.
  // Using `initial` as the value — actual values come from foundation/accent theme CSS.
  blocks.push('@theme {');
  for (const token of tokens) {
    const cssVar = String(token.css_var);
    blocks.push(`  ${cssVar}: initial;`);
  }
  blocks.push('}');
  blocks.push('');

  // Typography utility classes via @utility
  const roleSet = new Set();
  for (const token of tokens) {
    const match = String(token?.id || '').match(/^typography\.([a-z_]+)\.size$/u);
    if (match) roleSet.add(match[1]);
  }
  for (const role of [...roleSet].sort()) {
    const cssRole = toKebab(role);
    blocks.push(`@utility nimi-type-${cssRole} {`);
    blocks.push(`  font-size: var(--nimi-type-${cssRole}-size);`);
    blocks.push(`  line-height: var(--nimi-type-${cssRole}-line-height);`);
    blocks.push(`  font-weight: var(--nimi-type-${cssRole}-weight);`);
    blocks.push(`  letter-spacing: var(--nimi-type-${cssRole}-letter-spacing);`);
    if (role === 'mono') {
      blocks.push('  font-family: var(--nimi-font-mono);');
    }
    blocks.push('}');
    blocks.push('');
  }

  // Reduced motion
  blocks.push('@media (prefers-reduced-motion: reduce) {');
  blocks.push('  *, *::before, *::after {');
  blocks.push('    transition-duration: 0ms !important;');
  blocks.push('    animation-duration: 0ms !important;');
  blocks.push('  }');
  blocks.push('}');
  blocks.push('');

  return blocks.join('\n');
}

// ---------------------------------------------------------------------------
// Foundation theme CSS (light / dark)
// ---------------------------------------------------------------------------

function renderPackSelector(tokensDoc, pack, selector, onlyLayer = null) {
  const values = packValueMap(pack);
  const lines = [cssHeader('nimi-ui-themes.yaml'), `${selector} {`];
  for (const token of tokenRows(tokensDoc)) {
    const themeLayer = String(token?.theme_layer || 'foundation');
    if (onlyLayer && themeLayer !== onlyLayer) continue;
    const id = String(token.id);
    const cssVar = String(token.css_var);
    const value = values.get(id);
    if (!value) continue;
    lines.push(`  ${cssVar}: ${value};`);
  }
  lines.push('}');
  lines.push('');
  return lines.join('\n');
}

function renderFoundationTheme(tokensDoc, themesDoc, themeId, selector) {
  const pack = normalizePacks(themesDoc).find((item) => String(item?.theme_id) === themeId);
  if (!pack) throw new Error(`missing pack ${themeId}`);
  return renderPackSelector(tokensDoc, pack, selector, 'foundation');
}

// ---------------------------------------------------------------------------
// Accent theme CSS
// ---------------------------------------------------------------------------

function renderAccentTheme(tokensDoc, themesDoc, themeId) {
  const pack = normalizePacks(themesDoc).find((item) => String(item?.theme_id) === themeId);
  if (!pack) throw new Error(`missing pack ${themeId}`);
  const selector = `:root[data-nimi-accent="${themeId}"]`;
  return renderPackSelector(tokensDoc, pack, selector, 'accent');
}

function renderDesktopAccent(themeId, tokensDoc, themesDoc) {
  const selector = `:root[data-nimi-accent="${themeId}"]`;
  const lines = [renderAccentTheme(tokensDoc, themesDoc, themeId).trimEnd(), `${selector} {`];
  lines.push('  --font-ui: var(--nimi-font-sans);');
  lines.push('  --font-display: "Noto Sans SC", "Source Han Sans SC", "Inter", "PingFang SC", "Microsoft YaHei UI", "Segoe UI", system-ui, sans-serif;');
  lines.push('  --font-serif: "Noto Serif SC", "Source Han Serif SC", "Songti SC", "STSong", Georgia, serif;');
  lines.push('}');
  lines.push('');
  lines.push('@theme {');
  for (const [name, scale] of Object.entries(DESKTOP_ACCENT_PALETTE)) {
    for (const [step, value] of Object.entries(scale)) {
      lines.push(`  --color-${name}-${step}: ${value};`);
    }
  }
  lines.push('}');
  lines.push('');
  return lines.join('\n');
}

function renderRelayAccent(tokensDoc, themesDoc) {
  const selector = ':root[data-nimi-accent="relay-accent"]';
  return [
    renderAccentTheme(tokensDoc, themesDoc, 'relay-accent').trimEnd(),
    `${selector} {`,
    '  --nimi-accent-glow: 0 0 20px rgba(124, 111, 255, 0.15);',
    '}',
    '',
  ].join('\n');
}

function renderOvertoneAccent(tokensDoc, themesDoc) {
  const selector = ':root[data-nimi-accent="overtone-accent"]';
  return [
    renderAccentTheme(tokensDoc, themesDoc, 'overtone-accent').trimEnd(),
    `${selector} {`,
    '  --nimi-accent-glow: 0 0 20px rgba(139, 92, 246, 0.35), 0 0 60px rgba(139, 92, 246, 0.15);',
    '}',
    '',
  ].join('\n');
}

// ---------------------------------------------------------------------------
// main
// ---------------------------------------------------------------------------

async function main() {
  const checkMode = process.argv.includes('--check');
  const tokensDoc = await readYaml('nimi-ui-tokens.yaml');
  const themesDoc = await readYaml('nimi-ui-themes.yaml');

  const rendered = new Map([
    [path.join(outDir, 'tokens.ts'), renderTokensFile(tokensDoc, themesDoc)],
    [path.join(outDir, 'theme-base.css'), renderThemeBase(tokensDoc)],
    [path.join(themesDir, 'light.css'), renderFoundationTheme(tokensDoc, themesDoc, 'nimi-light', ':root:not([data-nimi-scheme]), :root[data-nimi-scheme="light"]')],
    [path.join(themesDir, 'dark.css'), renderFoundationTheme(tokensDoc, themesDoc, 'nimi-dark', '.dark, :root[data-nimi-scheme="dark"]')],
    [path.join(themesDir, 'desktop-accent.css'), renderDesktopAccent('desktop-accent', tokensDoc, themesDoc)],
    [path.join(themesDir, 'forge-accent.css'), renderDesktopAccent('forge-accent', tokensDoc, themesDoc)],
    [path.join(themesDir, 'relay-accent.css'), renderRelayAccent(tokensDoc, themesDoc)],
    [path.join(themesDir, 'overtone-accent.css'), renderOvertoneAccent(tokensDoc, themesDoc)],
    [path.join(themesDir, 'video-food-map-accent.css'), renderAccentTheme(tokensDoc, themesDoc, 'video-food-map-accent')],
  ]);

  if (checkMode) {
    const drifted = [];
    for (const [filePath, contents] of rendered) {
      let current = '';
      try {
        current = await fs.readFile(filePath, 'utf8');
      } catch {
        drifted.push(filePath);
        continue;
      }
      if (current !== contents) {
        drifted.push(filePath);
      }
    }
    if (drifted.length > 0) {
      process.stderr.write('nimi ui lib drift detected:\n');
      for (const item of drifted) {
        process.stderr.write(`  - ${path.relative(repoRoot, item)}\n`);
      }
      process.stderr.write('run `pnpm generate:nimi-ui-lib` to regenerate.\n');
      process.exitCode = 1;
      return;
    }
    const staleResolved = [];
    for (const filePath of LEGACY_GENERATED_FILES) {
      try {
        await fs.access(filePath);
        staleResolved.push(filePath);
      } catch {
        // already absent
      }
    }
    if (staleResolved.length > 0) {
      process.stderr.write('nimi ui lib contains legacy generated files:\n');
      for (const item of staleResolved) {
        process.stderr.write(`  - ${path.relative(repoRoot, item)}\n`);
      }
      process.stderr.write('run `pnpm generate:nimi-ui-lib` to clean legacy outputs.\n');
      process.exitCode = 1;
      return;
    }
    process.stdout.write(`nimi ui lib is up-to-date (${rendered.size} files)\n`);
    return;
  }

  await fs.mkdir(themesDir, { recursive: true });
  for (const legacyFile of LEGACY_GENERATED_FILES) {
    await fs.rm(legacyFile, { force: true });
  }
  for (const [filePath, contents] of rendered) {
    await fs.mkdir(path.dirname(filePath), { recursive: true });
    await fs.writeFile(filePath, contents, 'utf8');
  }
  process.stdout.write(`generated nimi ui lib projection (${rendered.size} files)\n`);
}

main().catch((error) => {
  process.stderr.write(`generate-nimi-ui-lib failed: ${String(error)}\n`);
  process.exitCode = 1;
});
