#!/usr/bin/env node

import { promises as fs } from 'node:fs';
import path from 'node:path';
import { fileURLToPath } from 'node:url';
import YAML from 'yaml';

const scriptDir = path.dirname(fileURLToPath(import.meta.url));
const repoRoot = path.resolve(scriptDir, '..');

const tablesDir = path.join(repoRoot, 'spec', 'platform', 'kernel', 'tables');
const outDir = path.join(repoRoot, 'spec', 'platform', 'kernel', 'generated');

const specs = [
  { input: 'protocol-error-codes.yaml', output: 'protocol-error-codes.md', render: renderErrorCodes },
  { input: 'protocol-primitives.yaml', output: 'protocol-primitives.md', render: renderPrimitives },
  { input: 'compliance-test-matrix.yaml', output: 'compliance-test-matrix.md', render: renderComplianceMatrix },
  { input: 'audit-events.yaml', output: 'audit-events.md', render: renderAuditEvents },
  { input: 'app-authorization-presets.yaml', output: 'app-authorization-presets.md', render: renderAuthPresets },
  { input: 'participant-profiles.yaml', output: 'participant-profiles.md', render: renderProfiles },
];

function normalizeMarkdown(markdown) {
  return `${markdown.replace(/\n{3,}/gu, '\n\n').replace(/\n+$/u, '\n')}`;
}

function header(title, sourceName) {
  return normalizeMarkdown([
    `# ${title}`,
    '',
    '> AUTO-GENERATED by `scripts/generate-platform-spec-kernel-docs.mjs`. DO NOT EDIT.',
    `> Source: \`spec/platform/kernel/tables/${sourceName}\``,
    '',
  ].join('\n'));
}

function renderErrorCodes(doc, sourceName) {
  const codes = Array.isArray(doc?.codes) ? doc.codes : [];
  let out = header('Generated Protocol Error Codes', sourceName);
  out += '| Name | Group | Source |\n';
  out += '|---|---|---|\n';
  for (const code of codes) {
    const name = String(code?.name || '').trim();
    const group = String(code?.group || '').trim();
    const source = String(code?.source || '').trim();
    if (!name) continue;
    out += `| \`${name}\` | \`${group}\` | \`${source}\` |\n`;
  }
  out += '\n';
  return normalizeMarkdown(out);
}

function renderPrimitives(doc, sourceName) {
  const primitives = Array.isArray(doc?.primitives) ? doc.primitives : [];
  let out = header('Generated Protocol Primitives', sourceName);
  for (const prim of primitives) {
    const name = String(prim?.name || '').trim();
    const source = String(prim?.source || '').trim();
    if (!name) continue;
    out += `## ${name} (${source})\n\n`;
    const fields = Array.isArray(prim?.fields) ? prim.fields : [];
    if (fields.length > 0) {
      out += '| Field | Type | Constraint |\n';
      out += '|---|---|---|\n';
      for (const field of fields) {
        out += `| \`${String(field?.field || '')}\` | \`${String(field?.type || '')}\` | ${String(field?.constraint || '')} |\n`;
      }
      out += '\n';
    }
    const rules = Array.isArray(prim?.rules) ? prim.rules : [];
    if (rules.length > 0) {
      out += '**Rules:**\n\n';
      for (const rule of rules) {
        out += `- ${String(rule)}\n`;
      }
      out += '\n';
    }
  }
  return normalizeMarkdown(out);
}

function renderComplianceMatrix(doc, sourceName) {
  const layers = Array.isArray(doc?.layers) ? doc.layers : [];
  let out = header('Generated Compliance Test Matrix', sourceName);
  for (const layer of layers) {
    const layerName = String(layer?.layer || '').trim();
    const label = String(layer?.label || '').trim();
    if (!layerName) continue;
    out += `## ${layerName}: ${label}\n\n`;
    const items = Array.isArray(layer?.items) ? layer.items : [];
    const hasReplay = items.some((i) => i?.replay_consistency);
    if (hasReplay) {
      out += '| Item | Positive | Rejection | Replay Consistency | Regression | Source |\n';
      out += '|---|---|---|---|---|---|\n';
    } else {
      out += '| Item | Positive | Rejection | Regression | Source |\n';
      out += '|---|---|---|---|---|\n';
    }
    for (const item of items) {
      const name = String(item?.item || '').trim();
      const positive = String(item?.positive || '').trim();
      const rejection = String(item?.rejection || '').trim();
      const regression = String(item?.regression || '').trim();
      const source = String(item?.source || '').trim();
      if (hasReplay) {
        const replay = String(item?.replay_consistency || '').trim();
        out += `| ${name} | ${positive} | ${rejection} | ${replay} | ${regression} | \`${source}\` |\n`;
      } else {
        out += `| ${name} | ${positive} | ${rejection} | ${regression} | \`${source}\` |\n`;
      }
    }
    out += '\n';
  }
  return normalizeMarkdown(out);
}

function renderAuditEvents(doc, sourceName) {
  const events = Array.isArray(doc?.events) ? doc.events : [];
  const requiredFields = Array.isArray(doc?.required_fields) ? doc.required_fields : [];
  let out = header('Generated Audit Events', sourceName);
  out += '## Events\n\n';
  out += '| Name | Group | Source |\n';
  out += '|---|---|---|\n';
  for (const event of events) {
    const name = String(event?.name || '').trim();
    const group = String(event?.group || '').trim();
    const source = String(event?.source || '').trim();
    if (!name) continue;
    out += `| \`${name}\` | \`${group}\` | \`${source}\` |\n`;
  }
  out += '\n## Required Audit Fields\n\n';
  for (const field of requiredFields) {
    out += `- \`${String(field)}\`\n`;
  }
  out += '\n';
  return normalizeMarkdown(out);
}

function renderAuthPresets(doc, sourceName) {
  const presets = Array.isArray(doc?.presets) ? doc.presets : [];
  const delegationRules = Array.isArray(doc?.delegation_rules) ? doc.delegation_rules : [];
  let out = header('Generated App Authorization Presets', sourceName);
  out += '## Presets\n\n';
  out += '| Name | Description | Can Delegate | Max Depth | Source |\n';
  out += '|---|---|---|---|---|\n';
  for (const preset of presets) {
    const name = String(preset?.name || '').trim();
    const desc = String(preset?.description || '').trim();
    const canDelegate = String(preset?.can_delegate ?? '').trim();
    const maxDepth = String(preset?.max_delegation_depth ?? '').trim();
    const source = String(preset?.source || '').trim();
    out += `| \`${name}\` | ${desc} | ${canDelegate} | ${maxDepth} | \`${source}\` |\n`;
  }
  out += '\n## Delegation Rules\n\n';
  for (const rule of delegationRules) {
    const text = String(rule?.rule || '').trim();
    const source = String(rule?.source || '').trim();
    out += `- ${text} (\`${source}\`)\n`;
  }
  out += '\n';
  return normalizeMarkdown(out);
}

function renderProfiles(doc, sourceName) {
  const profiles = Array.isArray(doc?.profiles) ? doc.profiles : [];
  const participantTypes = Array.isArray(doc?.participant_types) ? doc.participant_types : [];
  const appModes = Array.isArray(doc?.app_modes) ? doc.app_modes : [];
  let out = header('Generated Participant Profiles', sourceName);
  out += '## Profiles\n\n';
  for (const profile of profiles) {
    const pid = String(profile?.participant_id || '').trim();
    const ref = String(profile?.profile_ref || '').trim();
    const role = String(profile?.role || '').trim();
    const source = String(profile?.source || '').trim();
    out += `### ${pid} (\`${source}\`)\n\n`;
    out += `- Profile Ref: \`${ref}\`\n`;
    out += `- Role: ${role}\n\n`;
    const primCaps = Array.isArray(profile?.primitive_capabilities) ? profile.primitive_capabilities : [];
    if (primCaps.length > 0) {
      out += '| Primitive | Mode |\n';
      out += '|---|---|\n';
      for (const cap of primCaps) {
        out += `| \`${String(cap?.primitive || '')}\` | \`${String(cap?.mode || '')}\` |\n`;
      }
      out += '\n';
    }
    const domCaps = Array.isArray(profile?.domain_capabilities) ? profile.domain_capabilities : [];
    if (domCaps.length > 0) {
      out += '| Domain | Mode |\n';
      out += '|---|---|\n';
      for (const cap of domCaps) {
        out += `| \`${String(cap?.domain || '')}\` | \`${String(cap?.mode || '')}\` |\n`;
      }
      out += '\n';
    }
  }
  out += '## Participant Types\n\n';
  for (const pt of participantTypes) {
    out += `- \`${String(pt)}\`\n`;
  }
  out += '\n## App Modes\n\n';
  out += '| Name | Description | Source |\n';
  out += '|---|---|---|\n';
  for (const mode of appModes) {
    out += `| \`${String(mode?.name || '')}\` | ${String(mode?.description || '')} | \`${String(mode?.source || '')}\` |\n`;
  }
  out += '\n';
  return normalizeMarkdown(out);
}

function renderGeneratedIndex(entries) {
  let out = [
    '# Generated Platform Kernel Views',
    '',
    '> AUTO-GENERATED by `scripts/generate-platform-spec-kernel-docs.mjs`. DO NOT EDIT.',
    '',
    '| File | Source |',
    '|---|---|',
  ].join('\n');

  for (const entry of entries) {
    out += `\n| \`${entry.output}\` | \`tables/${entry.input}\` |`;
  }
  out += '\n';

  return normalizeMarkdown(out);
}

async function parseYamlFile(filePath) {
  const raw = await fs.readFile(filePath, 'utf8');
  return YAML.parse(raw);
}

async function main() {
  const checkMode = process.argv.includes('--check');

  await fs.mkdir(outDir, { recursive: true });

  const renderedEntries = [];
  for (const spec of specs) {
    const inputPath = path.join(tablesDir, spec.input);
    const outputPath = path.join(outDir, spec.output);
    const parsed = await parseYamlFile(inputPath);
    renderedEntries.push({ ...spec, outputPath, rendered: spec.render(parsed, spec.input) });
  }

  const indexPath = path.join(outDir, 'index.md');
  const indexRendered = renderGeneratedIndex(specs);

  if (checkMode) {
    const drifted = [];
    for (const entry of renderedEntries) {
      let current = '';
      try {
        current = await fs.readFile(entry.outputPath, 'utf8');
      } catch {
        drifted.push(entry.outputPath);
        continue;
      }
      if (current !== entry.rendered) drifted.push(entry.outputPath);
    }

    let currentIndex = '';
    try {
      currentIndex = await fs.readFile(indexPath, 'utf8');
    } catch {
      drifted.push(indexPath);
    }
    if (currentIndex !== indexRendered) drifted.push(indexPath);

    if (drifted.length > 0) {
      process.stderr.write('platform kernel generated docs drift detected:\n');
      for (const file of drifted) {
        process.stderr.write(`  - ${path.relative(repoRoot, file)}\n`);
      }
      process.stderr.write('run `pnpm generate:platform-spec-kernel-docs` to regenerate.\n');
      process.exitCode = 1;
      return;
    }
    process.stdout.write(`platform kernel generated docs are up-to-date (${renderedEntries.length + 1} files)\n`);
    return;
  }

  for (const entry of renderedEntries) {
    await fs.writeFile(entry.outputPath, entry.rendered, 'utf8');
  }
  await fs.writeFile(indexPath, indexRendered, 'utf8');
  process.stdout.write(`generated platform kernel docs (${renderedEntries.length + 1} files)\n`);
}

main().catch((error) => {
  process.stderr.write(`generate-platform-spec-kernel-docs failed: ${String(error)}\n`);
  process.exitCode = 1;
});
