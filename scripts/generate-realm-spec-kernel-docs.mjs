#!/usr/bin/env node

import fs from 'node:fs';
import path from 'node:path';
import YAML from 'yaml';

const PROJECT_ROOT = process.cwd();
const KERNEL_DIR = path.join(PROJECT_ROOT, 'spec', 'realm', 'kernel');
const TABLES_DIR = path.join(KERNEL_DIR, 'tables');
const GENERATED_DIR = path.join(KERNEL_DIR, 'generated');
const CHECK_MODE = process.argv.includes('--check');

function toPosix(filePath) {
  return filePath.replace(/\\/g, '/');
}

function relativeToRoot(absPath) {
  return toPosix(path.relative(PROJECT_ROOT, absPath));
}

function readYaml(absPath) {
  return YAML.parse(fs.readFileSync(absPath, 'utf8'));
}

function asStringArray(value) {
  if (!Array.isArray(value)) return [];
  return value.filter((item) => typeof item === 'string' && item.trim().length > 0);
}

function escapeCell(value) {
  return String(value ?? '')
    .replace(/\|/g, '\\|')
    .replace(/\r?\n/g, '<br/>')
    .trim();
}

function withPreamble(title, sourceFile, bodyLines) {
  return [
    '<!-- DO NOT EDIT: generated by scripts/generate-realm-spec-kernel-docs.mjs -->',
    `<!-- Source: ${relativeToRoot(sourceFile)} -->`,
    '',
    `# ${title}`,
    '',
    ...bodyLines,
    '',
  ].join('\n');
}

function formatTitle(stem) {
  return stem
    .split(/[-_]/g)
    .map((part) => part.charAt(0).toUpperCase() + part.slice(1))
    .join(' ');
}

function renderRuleCatalog(doc, sourceFile) {
  const data = doc ?? {};
  const rules = Array.isArray(data.rules) ? data.rules : [];
  const blockedRules = Array.isArray(data.blocked_rules) ? data.blocked_rules : [];
  const lines = [
    `Generated at: ${data.generated_at ?? 'unknown'}`,
    '',
    `Total rules: ${rules.length}`,
    `Blocked external rules: ${blockedRules.length}`,
    '',
    '| Rule ID | Domain | Level | Source | Statement |',
    '| --- | --- | --- | --- | --- |',
  ];
  for (const row of rules) {
    lines.push(
      `| ${escapeCell(row.rule_id)} | ${escapeCell(row.domain)} | ${escapeCell(row.level)} | ${escapeCell(row.source)} | ${escapeCell(row.statement)} |`,
    );
  }
  if (blockedRules.length > 0) {
    lines.push('', '| Blocked Rule ID | Status | Blocker | Summary |', '| --- | --- | --- | --- |');
    for (const row of blockedRules) {
      lines.push(
        `| ${escapeCell(row.rule_id)} | ${escapeCell(row.status)} | ${escapeCell(row.blocker_id)} | ${escapeCell(row.summary)} |`,
      );
    }
  }
  return withPreamble('Rule Catalog (Generated)', sourceFile, lines);
}

function renderContract(doc, sourceFile, title) {
  const data = doc ?? {};
  const rules = Array.isArray(data.rules) ? data.rules : [];
  const entities = Array.isArray(data.entities) ? data.entities : [];
  const requiredOps = asStringArray(data.api_surface?.required_operations);
  const secondaryOps = asStringArray(data.api_surface?.secondary_operations);
  const lines = [
    `Contract: \`${data.contract_id ?? 'unknown'}\``,
    `Domain: \`${data.domain ?? 'unknown'}\``,
    `Version: \`${data.version ?? 'unknown'}\``,
    '',
    `Rules: ${rules.length}`,
    '',
    '| Rule ID | Level | Title | Statement |',
    '| --- | --- | --- | --- |',
  ];
  for (const row of rules) {
    lines.push(
      `| ${escapeCell(row.rule_id)} | ${escapeCell(row.level)} | ${escapeCell(row.title)} | ${escapeCell(row.statement)} |`,
    );
  }
  lines.push('', `Entities: ${entities.length}`, '', '| Entity | Prisma Model | Required Fields | JSON Fields |', '| --- | --- | --- | --- |');
  for (const entity of entities) {
    lines.push(
      `| ${escapeCell(entity.name)} | ${escapeCell(entity.prisma_model)} | ${escapeCell(asStringArray(entity.required_fields).join(', '))} | ${escapeCell(asStringArray(entity.json_fields).join(', '))} |`,
    );
  }
  lines.push('', `Required operations: ${requiredOps.length}`);
  for (const operation of requiredOps) lines.push(`- ${operation}`);
  lines.push('', `Secondary operations: ${secondaryOps.length}`);
  for (const operation of secondaryOps) lines.push(`- ${operation}`);
  return withPreamble(title, sourceFile, lines);
}

function resolveRuleEvidence(profiles, entry) {
  const resolved = {
    openapi: [],
    prisma_models: [],
    service_files: [],
    test_files: [],
  };
  const merge = (source) => {
    if (!source) return;
    resolved.openapi.push(...asStringArray(source.openapi));
    resolved.prisma_models.push(...asStringArray(source.prisma_models));
    resolved.service_files.push(...asStringArray(source.service_files));
    resolved.test_files.push(...asStringArray(source.test_files));
  };
  for (const profileRef of asStringArray(entry.profile_refs)) merge(profiles[profileRef]);
  merge(entry);
  resolved.openapi = [...new Set(resolved.openapi)];
  resolved.prisma_models = [...new Set(resolved.prisma_models)];
  resolved.service_files = [...new Set(resolved.service_files)];
  resolved.test_files = [...new Set(resolved.test_files)];
  return resolved;
}

function renderRuleEvidence(doc, sourceFile) {
  const data = doc ?? {};
  const rules = data.rules ?? {};
  const profiles = data.profiles ?? {};
  const lines = [
    `Version: \`${data.version ?? 'unknown'}\``,
    `Owner: \`${data.owner ?? 'unknown'}\``,
    '',
    `Profiles: ${Object.keys(profiles).length}`,
    `Rules mapped: ${Object.keys(rules).length}`,
    '',
    '| Rule ID | Domain | OpenAPI | Prisma | Services | Tests |',
    '| --- | --- | --- | --- | --- | --- |',
  ];
  for (const [ruleId, entry] of Object.entries(rules)) {
    const resolved = resolveRuleEvidence(profiles, entry);
    lines.push(
      `| ${escapeCell(ruleId)} | ${escapeCell(entry.domain)} | ${escapeCell(resolved.openapi.length)} | ${escapeCell(resolved.prisma_models.length)} | ${escapeCell(resolved.service_files.length)} | ${escapeCell(resolved.test_files.length)} |`,
    );
  }
  return withPreamble('Rule Evidence Map (Generated)', sourceFile, lines);
}

function renderDomainEnums(doc, sourceFile) {
  const data = doc ?? {};
  const rows = Array.isArray(data.enums) ? data.enums : [];
  const lines = [
    `Version: \`${data.version ?? 'unknown'}\``,
    `Updated: \`${data.updated_at ?? 'unknown'}\``,
    `Scope note: ${data.scope_note ?? 'n/a'}`,
    '',
    '| Enum ID | Domain | Values | Source Rules | Status | Blocker | Notes |',
    '| --- | --- | --- | --- | --- | --- | --- |',
  ];
  for (const row of rows) {
    lines.push(
      `| ${escapeCell(row.enum_id)} | ${escapeCell(row.domain)} | ${escapeCell(asStringArray(row.values).join(', '))} | ${escapeCell(asStringArray(row.source_rules).join(', '))} | ${escapeCell(row.status || 'active')} | ${escapeCell(row.blocker_id || '')} | ${escapeCell(row.notes || '')} |`,
    );
  }
  return withPreamble('Domain Enums (Generated)', sourceFile, lines);
}

function renderStateMachines(doc, sourceFile) {
  const data = doc ?? {};
  const rows = Array.isArray(data.state_machines) ? data.state_machines : [];
  const lines = [
    `Version: \`${data.version ?? 'unknown'}\``,
    `Updated: \`${data.updated_at ?? 'unknown'}\``,
    '',
  ];
  for (const row of rows) {
    lines.push(`## ${row.machine_id ?? 'UNKNOWN'}`);
    lines.push(`- Domain: ${row.domain ?? 'unknown'}`);
    lines.push(`- Initial: ${row.initial_state ?? 'unknown'}`);
    lines.push(`- States: ${asStringArray(row.states).join(', ')}`);
    lines.push(`- Source Rules: ${asStringArray(row.source_rules).join(', ')}`);
    lines.push('- Transitions:');
    for (const transition of Array.isArray(row.transitions) ? row.transitions : []) {
      lines.push(`  - ${transition.from ?? '?'} -> ${transition.to ?? '?'} on \`${transition.event ?? 'unknown'}\``);
    }
    lines.push('');
  }
  while (lines.length > 0 && lines[lines.length - 1] === '') {
    lines.pop();
  }
  return withPreamble('Domain State Machines (Generated)', sourceFile, lines);
}

