function renderGenericYaml(doc, sourceFile, stem) {
  const data = doc ?? {};
  const lines = [
    '```yaml',
    fs.readFileSync(sourceFile, 'utf8').trim(),
    '```',
    '',
    `Top-level keys: ${Object.keys(data).join(', ')}`,
  ];
  return withPreamble(`${formatTitle(stem)} (Generated)`, sourceFile, lines);
}

function buildRenderTargets() {
  const tableFiles = fs
    .readdirSync(TABLES_DIR)
    .filter((file) => file.endsWith('.yaml'))
    .sort((a, b) => a.localeCompare(b));
  const targets = [];
  for (const file of tableFiles) {
    const sourceFile = path.join(TABLES_DIR, file);
    const stem = file.replace(/\.yaml$/u, '');
    const outputFile = path.join(GENERATED_DIR, `${stem}.md`);
    const doc = readYaml(sourceFile);
    let content;
    if (file === 'rule-catalog.yaml') {
      content = renderRuleCatalog(doc, sourceFile);
    } else if (file === 'rule-evidence.yaml') {
      content = renderRuleEvidence(doc, sourceFile);
    } else if (file === 'domain-enums.yaml') {
      content = renderDomainEnums(doc, sourceFile);
    } else if (file === 'domain-state-machines.yaml') {
      content = renderStateMachines(doc, sourceFile);
    } else if (file === 'open-spec-alignment-map.yaml') {
      content = renderOpenSpecAlignment(doc, sourceFile);
    } else if (file === 'under-spec-registry.yaml') {
      content = renderUnderSpec(doc, sourceFile);
    } else if (file.endsWith('-contract.yaml')) {
      content = renderContract(doc, sourceFile, `${formatTitle(stem)} (Generated)`);
    } else {
      content = renderGenericYaml(doc, sourceFile, stem);
    }
    targets.push({ sourceFile, outputFile, content });
  }
  return targets;
}

function buildIndexContent(targets) {
  const lines = ['Generated files:', ''];
  for (const target of targets) {
    lines.push(`- \`${relativeToRoot(target.outputFile)}\``);
  }
  return withPreamble('Realm Kernel Generated Index', path.join(KERNEL_DIR, 'index.md'), lines);
}

function ensureDir(dir) {
  fs.mkdirSync(dir, { recursive: true });
}

function writeFile(absPath, content) {
  ensureDir(path.dirname(absPath));
  fs.writeFileSync(absPath, content, 'utf8');
}

function verifyOrWrite(targets, indexContent) {
  const mismatches = [];
  for (const target of targets) {
    const existing = fs.existsSync(target.outputFile) ? fs.readFileSync(target.outputFile, 'utf8') : null;
    if (CHECK_MODE) {
      if (existing !== target.content) mismatches.push(relativeToRoot(target.outputFile));
      continue;
    }
    writeFile(target.outputFile, target.content);
  }
  const indexPath = path.join(GENERATED_DIR, 'index.md');
  const existingIndex = fs.existsSync(indexPath) ? fs.readFileSync(indexPath, 'utf8') : null;
  if (CHECK_MODE) {
    if (existingIndex !== indexContent) mismatches.push(relativeToRoot(indexPath));
    if (mismatches.length > 0) {
      throw new Error(`Realm kernel generated docs drift detected:\n- ${mismatches.join('\n- ')}`);
    }
    return;
  }
  writeFile(indexPath, indexContent);
}

function removeStaleGeneratedFiles(targets) {
  const expected = new Set(targets.map((target) => path.basename(target.outputFile)).concat('index.md'));
  if (!fs.existsSync(GENERATED_DIR)) return;
  for (const entry of fs.readdirSync(GENERATED_DIR)) {
    if (!entry.endsWith('.md')) continue;
    if (expected.has(entry)) continue;
    fs.rmSync(path.join(GENERATED_DIR, entry), { force: true });
  }
}

function main() {
  const targets = buildRenderTargets();
  const indexContent = buildIndexContent(targets);
  if (!CHECK_MODE) removeStaleGeneratedFiles(targets);
  verifyOrWrite(targets, indexContent);
}

main();
